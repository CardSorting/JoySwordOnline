function PETIT_DARKBRINGER_UNIRING_2_ATTACK_FRAME_MOVE( pKTDXApp, pX2Game, pPet )

    if pPet:AnimEventTimer( 0.001 ) then
		local pEffectSet = pX2Game:GetEffectSet()
		if pEffectSet ~= nil then
			local pTargetUnit = pPet:GetTargetUnit()			
			local hEffect =  pEffectSet:PlayEffectSetByPet_LUA( "EffectSet_PETIT_DARKBRINGER_UNIRING_2", pPet )
			if pPet:IsAttacking() == true then
				pEffectSet:SetEffectSetInstPos( hEffect, pTargetUnit:GetPos(), true )
			else 
				pEffectSet:SetEffectSetInstPos( hEffect, pX2Game:GetNearestNPCUnitPos_LUA( pPet:GetPos(), 0, 10000 ), true )
			end
		end
	end
end

function CF_PETATTACK_PETIT_DARKBRINGER_UNIRING_2( pKTDXApp, pX2Game, pPet )

	if pX2Game ~= nil then
		if pX2Game:IsHost() == false then
			return false
		end
	end

	if pPet:IsAttacking() == true then

		local fDist = pPet:GetDistTarget()
		if fDist < 300 then
			return true
		end
	end

	return false
end

function PETIT_DARKBRINGER_UNIRING_2_SPECIAL_FRAME_MOVE( pKTDXApp, pX2Game, pPet )

    if pPet:AnimEventTimer( 0.001 ) then
		local pEffectSet = pPet:GetUIEffectSet()
		if pEffectSet ~= nil then
			pEffectSet:PlayEffectSetByPet_LUA( "EffectSet_Pet_Unicorn_Special", pPet )
		end
    end
end