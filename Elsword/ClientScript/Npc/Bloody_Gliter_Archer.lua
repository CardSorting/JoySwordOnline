function BLOODY_GLITER_ARCHER_COMMON_FRAME_FUNC( pKTDApp, pX2Game, pNPCUnit )
	if nil ~= pNPCUnit and false == pNPCUnit:GetFlag_LUA( 0 ) then
		local maxHP = pNPCUnit:GetMaxHP()
		
		if pNPCUnit:GetNowHP() < maxHP * 0.4 then
			pNPCUnit:StateChange_LUA( "BLOODY_GLITER_ARCHER_DRINK_POTION", false )
			local index = pX2Game:GetNowStageIndex()
			pNPCUnit:SetFlag_LUA( 0, true )
			pNPCUnit:SetInt_LUA( 0, index )
		end
	end
end
