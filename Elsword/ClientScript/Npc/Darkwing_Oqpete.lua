	    pNPCUnit:StateChange_LUA("DARKWING_OQPETE_DYING_SKY", true)
	else
		local bIsFly = pNPCUnit:GetFlag_LUA( 0 )	-- 현재 채공 여부
		
		if false == bIsFly then				-- 공중에서 맞았을 때, 지상 착륙 중이면 한바퀴 도는 모션으로 설정
