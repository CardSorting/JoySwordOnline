function DEATH_SCYTHE_3_CUTE1_FRAME_MOVE( pKTDXApp, pX2Game, pPet )

    if pPet:AnimEventTimer( 0.001 ) then
		local pEffectSet = pPet:GetUIEffectSet()
		if pEffectSet ~= nil then
			pEffectSet:PlayEffectSetByPet_LUA( "EffectSet_Pet_Mystic_C_Cute1", pPet )
		end
	end
end

function DEATH_SCYTHE_3_ATTACK_FRAME_MOVE( pKTDXApp, pX2Game, pPet )
	
	if pPet:AnimEventTimer( 0.6 ) then
		local pDamageEffect = pX2Game:GetDamageEffect()
		if pDamageEffect ~= nil then
	        local pMasterUnit = pPet:GetMaster()
	        if pMasterUnit ~= nil and pMasterUnit:GetNowHP() > 0 then
				local vLandPos = pPet:GetLandPosition_LUA()
				local vTargetPos = pX2Game:GetNearestNPCUnitPos_LUA(pMasterUnit:GetPos(), 0, 999999)
				local vAttackPos = pPet:GetBonePos_LUA("Bip01_L_Finger0Nub")
				local vAttackDir = D3DXVECTOR3(vTargetPos.x - vAttackPos.x, vTargetPos.y - vAttackPos.y, vTargetPos.z - vAttackPos.z)
				local vAttackDegree = GetDirVecToDegree( vAttackDir )
				local pPetAttack = pDamageEffect:CreateInstanceByPet_LUA( pMasterUnit, "PET_DEATHSCYTHE_ATTACK3", vAttackPos, vTargetPos, vAttackDegree )
			end
		end
	end
	
end

function CF_PETATTACK_DEATH_SCYTHE_3( pKTDXApp, pX2Game, pPet )

	if pX2Game ~= nil then
		if pX2Game:IsHost() == false then
			return false
		end
	end

	if pPet:IsAttacking() == true then

		local fDist = pPet:GetDistTarget()
		if fDist <= 99999 then
			return true
		end
	end

	return false
end

