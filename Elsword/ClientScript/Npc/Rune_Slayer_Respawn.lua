function CF_WATERWAY_CORE_RESPAWNER_RESPAWN( pKTDXApp, pX2Game, pNPCUnit )

	nNPC1		= pX2Game:LiveNPCNumType_LUA( NPC_UNIT_ID["NUI_RUNE_SLAYER_CORRUPTION"] )
	nNPC2		= pX2Game:LiveNPCNumType_LUA( NPC_UNIT_ID["NUI_LORD_KNIGHT_CORRUPTION"] )
	nNPC3		= pX2Game:LiveNPCNumType_LUA( NPC_UNIT_ID["NUI_RUNE_SLAYER_CORRUPTION"] )
	nNPC4		= pX2Game:LiveNPCNumType_LUA( NPC_UNIT_ID["NUI_LORD_KNIGHT_CORRUPTION"] )
	nNPC5		= pX2Game:LiveNPCNumType_LUA( NPC_UNIT_ID["NUI_LORD_KNIGHT_CORRUPTION"] )
	
	nNPC		= nNPC1	+	nNPC2	+	nNPC3	+	nNPC4	+	nNPC5 
		
	if nNPC < pNPCUnit:GetInt_LUA(0) then
		return true
	else
		return false
	end
	
end


function WATERWAY_CORE_RESPAWNER_RESPAWN_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )

	if pNPCUnit:AnimEventTimer_LUA( 0.4 ) then		
		pos = pNPCUnit:GetPos()
		
		-- pos.x = pox.x + 250
		--pos.y = pos.y - 30
				
		pX2Game:CreateNPCReq_LUA(  687 + pNPCUnit:GetRandVal() % 5 , pNPCUnit:GetHardLevel(), TRUE, pos, pNPCUnit:GetIsRight(), 0, true  )
		
		if pX2Game:IsHost() then 
			nowHP = pNPCUnit:GetNowHP()			
			pNPCUnit:SetNowHP_LUA( nowHP - 1000.0 )
		end
	end
		
end

