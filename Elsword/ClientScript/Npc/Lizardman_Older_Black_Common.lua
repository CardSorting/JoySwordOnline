function LIZARDMAN_OLDER_BLACK_COMMON_MAGIC_ATTACKA_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )

	
	if pNPCUnit:AnimEventTimer_LUA( 0.2 ) then	

		unitPos = pNPCUnit:GetNearestUserPos()
		unitLandPos = pX2Game:GetLineMap():GetLandPosition_LUA( unitPos )
		pNPCUnit:SetVector_LUA( 0, unitLandPos )
		
		pMinorParticle = pX2Game:GetMinorParticle()
		pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "LizardMan_ShamanKing_SP_HyperFrame01_Warning", unitLandPos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )

	end

	-- 플레이어 윗쪽에 라이트닝을 떨어뜨린다~
	if pNPCUnit:AnimEventTimer_LUA( 0.9 ) then
		
		unitLandPos = pNPCUnit:GetVector_LUA( 0 )
				
		pDamageEffect = pX2Game:GetDamageEffect()		
		pDamageEffect:CreateInstance_LUA( pNPCUnit, "LIZARDMAN_OLDER_BLACK_COMMON_MAGIC_ATTACK_A", unitLandPos, unitLandPos.y )
		
		pMajorParticle = pX2Game:GetMajorParticle()
		pMajorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "LizardMan_Older_Lightning02", unitLandPos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
		pMajorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "LizardMan_Older_Lightning03", unitLandPos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
		
	end
	
		
	if pNPCUnit:AnimEventTimer_LUA( 0.2 ) then	
	
		bIsRight = pNPCUnit:GetIsRight()
		vDirVector = pNPCUnit:GetDirVector()
		vPos = pNPCUnit:GetLandPosition_LUA()
		if bIsRight == false then
			vPos = MovePos( vPos, vDirVector, -220 )
		else
			vPos = MovePos( vPos, vDirVector, 220 )
		end
		
		pNPCUnit:SetVector_LUA( 1, vPos )
		
		pMinorParticle = pX2Game:GetMinorParticle()
		pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "LizardMan_ShamanKing_SP_HyperFrame01_Warning", vPos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )

	end
	
	
	-- 자신의 앞쪽에 라이트닝을 떨어뜨린다
	if pNPCUnit:AnimEventTimer_LUA( 0.6 ) then
		
		unitLandPos = pNPCUnit:GetVector_LUA( 1 )
				
		pDamageEffect = pX2Game:GetDamageEffect()		
		pDamageEffect:CreateInstance_LUA( pNPCUnit, "LIZARDMAN_OLDER_BLACK_COMMON_MAGIC_ATTACK_A", unitLandPos, unitLandPos.y )
		
		pMajorParticle = pX2Game:GetMajorParticle()
		pMajorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "LizardMan_Older_Lightning02", unitLandPos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
		pMajorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "LizardMan_Older_Lightning03", unitLandPos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
		
	end
	
	
end
	
	
	
	
