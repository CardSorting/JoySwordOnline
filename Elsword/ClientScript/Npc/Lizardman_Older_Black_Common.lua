function LIZARDMAN_OLDER_BLACK_COMMON_ATTACK_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )
		
	if pNPCUnit:AnimEventTimer_LUA( 0.725 ) then
	
		vLandPos = pNPCUnit:GetLandPosition_LUA()		
		vBonePos = pNPCUnit:GetWeaponBonePos_LUA( 0, "ATTACK_LINE_START0" )
		pDamageEffect = pX2Game:GetDamageEffect()
		pDamageEffect:CreateInstance_LUA( pNPCUnit, "LIZARDMAN_BLUE_FIRE_BALL", vBonePos, vLandPos.y )
		
	end
	

	if pNPCUnit:AnimEventTimer_LUA( 0.01 ) then
	
		pMinorParticle = pX2Game:GetMinorParticle()
	
	
		vRotateDegree	= pNPCUnit:GetRotateDegree()
		vBonePos = pNPCUnit:GetWeaponBonePos_LUA( 0, "ATTACK_LINE_START0" )
	
		pParticle = pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "LizardMan_Older_FireAttack", vBonePos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
		if pParticle ~= nil then 
			pParticle:SetAddRotate( vRotateDegree )
			pParticle:SetAxisAngle( vRotateDegree )	
			pNPCUnit:SetMinorParticle_LUA( 0, pParticle:GetHandle() ) 
		end
	
	end
	
	
	pParticle = pNPCUnit:GetMinorParticle_LUA( 0 ) 
	if nil ~= pParticle then 
	
		if pMinorParticle:IsLiveInstance( pParticle ) == true then 
			vBonePos = pNPCUnit:GetWeaponBonePos_LUA( 0, "ATTACK_LINE_START0" )
			pParticle:SetPosition( vBonePos )		
		else 
			pNPCUnit:ClearMinorParticle_LUA( 0 )
		end
	
	end

end



