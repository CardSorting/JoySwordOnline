-- 애머시스트의 이펙트를 그대로 이용하는 바람에 NPC 모션과 데미지 이펙트가 생성되는 타이밍이 맞지가 않다.
-- 따라서 해당 모션이 끝날 때 (취소될 때) 이미 데미지 이펙트가 발사된 상태라면 놔두고, 그렇지 않다면
-- 해당 이펙트를 삭제해서 몬스터의 공격이 취소되었는데 데미지 이펙트가 나오는 일이 없도록 한다.
-- 여기에 MAGICATTACKA_FIRED_FLAG, MAGICATTACKB_FIRED_FLAG를 사용했다.
function GLITER_NECROMANCER_BOSS_MAGIC_ATTACK_STATE_START( pKTDXApp, pX2Game, pNPCUnit )
	pNPCUnit:SetFlag_LUA(MAGICATTACKA_FIRED_FLAG, false)
end

function GLITER_NECROMANCER_BOSS_MAGIC_ATTACK_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )

	if pNPCUnit:AnimEventTimer_LUA( 0.001 ) then	
		vLandPos = pNPCUnit:GetLandPosition_LUA()
		
		pDamageEffect = pX2Game:GetDamageEffect()
		if pDamageEffect ~= nil then
		    pMagicAttackA = pDamageEffect:CreateInstance_LUA( pNPCUnit, "AMETHYST_MAGICATTACK_A", vLandPos, vLandPos.y )
		end	
	end	
	
	if pNPCUnit:AnimEventTimer_LUA( 2.020 ) then
		pNPCUnit:SetFlag_LUA(MAGICATTACKA_FIRED_FLAG, true)	
	end
end

function GLITER_NECROMANCER_BOSS_MAGIC_ATTACK_STATE_END( pKTDXApp, pX2Game, pNPCUnit )

	pDamageEffect = pX2Game:GetDamageEffect()
	if pMagicAttackA ~= nil and pNPCUnit:GetFlag_LUA(MAGICATTACKA_FIRED_FLAG) == false then
		pDamageEffect:DestroyInstance(pMagicAttackA)
	end
end


function GLITER_NECROMANCER_BOSS_MAGIC_ATTACK_B_STATE_START( pKTDXApp, pX2Game, pNPCUnit )
	pNPCUnit:SetFlag_LUA(MAGICATTACKB_FIRED_FLAG, false)
end

function GLITER_NECROMANCER_BOSS_MAGIC_ATTACK_B_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )
	
	if pNPCUnit:AnimEventTimer_LUA( 1.0 ) then	
		vLandPos = pNPCUnit:GetLandPosition_LUA()
		
		pDamageEffect = pX2Game:GetDamageEffect()
		if pDamageEffect ~= nil then
		    pMagicAttackB = pDamageEffect:CreateInstance_LUA( pNPCUnit, "AMETHYST_MAGICATTACK_C", vLandPos, vLandPos.y )
		end	
	end	
	
	if pNPCUnit:AnimEventTimer_LUA( 0.400 ) then
		pMinorParticle = pX2Game:GetMinorParticle()
		--pos = pNPCUnit:GetBonePos_LUA("Bip01")
		pos = pNPCUnit:GetLandPosition_LUA()
  		pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "Necromancer_DarkCloud02_UnderLight", pos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
		--pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "Necromancer_DarkCloud03_BoosterLight", pos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
 	end
	
	if pNPCUnit:AnimEventTimer_LUA( 0.90 ) then
		pMinorParticle = pX2Game:GetMinorParticle()
		--pos = pNPCUnit:GetBonePos_LUA("Bip01")
		pos = pNPCUnit:GetLandPosition_LUA()
		pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "Necromancer_DarkCloud02_UnderLight", pos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
		pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "Necromancer_DarkCloud03_BoosterLight", pos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
		pX2Game:GetX2Camera():GetCamera():UpDownCrashCameraNoReset( 10.0, 1.7 )
 	end
	
	if pNPCUnit:AnimEventTimer_LUA( 1.8 ) then
		pNPCUnit:SetFlag_LUA(MAGICATTACKB_FIRED_FLAG, true)			
	end
	
	
end


function GLITER_NECROMANCER_BOSS_MAGIC_ATTACK_B_STATE_END( pKTDXApp, pX2Game, pNPCUnit )

	pDamageEffect = pX2Game:GetDamageEffect()
	if pMagicAttackB ~= nil and pNPCUnit:GetFlag_LUA(MAGICATTACKB_FIRED_FLAG) == false then
		pDamageEffect:DestroyInstance(pMagicAttackB)
	end
end


function GLITER_NECROMANCER_BOSS_DARK_CLOUD_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )

	if pNPCUnit:AnimEventTimer_LUA( 0.500 ) then
		pMinorParticle = pX2Game:GetMinorParticle()
		--pos = pNPCUnit:GetBonePos_LUA("Bip01")
		pos = pNPCUnit:GetLandPosition_LUA()
  		pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "Necromancer_DarkCloud02_UnderLight", pos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
		pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "Necromancer_DarkCloud03_BoosterLight", pos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
 	end


	if pNPCUnit:AnimEventTimer_LUA( 1.573 ) then
		if GetDistance_LUA( pNPCUnit:GetPos(), pX2Game:GetFocusUnitPos_LUA() ) < 1200 then
			pX2Game:GetX2Camera():GetCamera():UpDownCrashCameraNoReset( 15, 0.2 )
		end
        pDamageEffect = pX2Game:GetDamageEffect()
		pos = pNPCUnit:GetLandPosition_LUA()
		pDamageEffect:CreateInstance_LUA( pNPCUnit, "GLITER_NECROMANCER_BOSS_DARK_CLOUD", pNPCUnit:GetWeaponBonePos_LUA( 0, "Weapon1" ), pos.y )
	end
end


