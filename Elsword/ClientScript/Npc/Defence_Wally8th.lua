function WALLY_8TH_JUMP_BOOST_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )

	pMeshPlayerL = pNPCUnit:GetMajorMeshPlayer_LUA( 0 )
	pMeshPlayerR = pNPCUnit:GetMajorMeshPlayer_LUA( 1 )
	
	
	if pMeshPlayerL ~= nil then 
		if pX2Game:GetMajorXMeshPlayer():IsLiveInstance(pMeshPlayerL) == false then 
			pNPCUnit:ClearMajorMeshPlayer_LUA( 0 )
		end
	end
	
	if pMeshPlayerR ~= nil then 
		if pX2Game:GetMajorXMeshPlayer():IsLiveInstance(pMeshPlayerR) == false then 
			pNPCUnit:ClearMajorMeshPlayer_LUA( 1 )
		end
	end
	


	rotDegree 	= pNPCUnit:GetRotateDegree()
	LHandPos 	= pNPCUnit:GetBonePos_LUA("Bip01_L_Finger0")
	RHandPos 	= pNPCUnit:GetBonePos_LUA("Bip01_R_Finger0")
	
	
	pMeshPlayerL = pNPCUnit:GetMajorMeshPlayer_LUA( 0 )
	pMeshPlayerR = pNPCUnit:GetMajorMeshPlayer_LUA( 1 )
	
		
	if pMeshPlayerL ~= nil then
		pMeshPlayerL:SetPos( LHandPos ) 
		pMeshPlayerL:SetRotateDegree( rotDegree )
		pMeshPlayerL:SetMoveAxisAngleDegree( rotDegree ) 
	end
	
	if pMeshPlayerR ~= nil then 
		pMeshPlayerR:SetPos( RHandPos ) 
		pMeshPlayerR:SetRotateDegree( rotDegree )
		pMeshPlayerR:SetMoveAxisAngleDegree( rotDegree ) 
	end
	
end







