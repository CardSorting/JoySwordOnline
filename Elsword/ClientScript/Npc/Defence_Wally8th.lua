function WALLY_8TH_WALK_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )
	
	if pX2Game:AnyUserUnitInRange( pNPCUnit:GetPos(), 200 ) == true then
		TargetPos = pX2Game:GetNearestUserUnitPos_LUA( pNPCUnit:GetPos(), 0, 999999 )
	        
		bIsRight = pNPCUnit:GetIsRight()
	        
		if pNPCUnit:IsRightTarget_LUA(pNPCUnit, TargetPos) == true then
			if( bIsRight == true ) then
				pNPCUnit:StateChange_LUA( "WALLY_8TH_COMBO1" )
			end
		else
			if( bIsRight == false ) then
				pNPCUnit:StateChange_LUA( "WALLY_8TH_COMBO1" )
			end
		end
	end
end



function WALLY_8TH_WAIT_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )
	
