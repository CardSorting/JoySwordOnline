function GLITER_HAMMER_GREAT_ATTACK_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )
	
	if pNPCUnit:AnimEventTimer_LUA( 0.22 ) then
	
		local vDirVector		= pNPCUnit:GetDirVector()
		local bIsRight		= pNPCUnit:GetIsRight()
		local rotDegree		= pNPCUnit:GetRotateDegree()
		
		local pMinorParticle = pX2Game:GetMinorParticle()
		local pos = pNPCUnit:GetLandPosition_LUA()
		
		if bIsRight == true then 
			pos = MovePos( pos, vDirVector, 240 * 1.2 )     -- 원본: pos = MovePos( pos, vDirVector, 240 * INIT_SYSTEM["UNIT_SCALE"] )
		else
			pos = MovePos( pos, vDirVector, -240 * 1.2 )
		end
		
		
		pMinorParticle:CreateSequence_LUA( "DownSmoke", pos, D3DXVECTOR2(120,120), D3DXVECTOR2(10,-1) )
		pos.y = pos.y + 5
		pMinorParticle:CreateSequence_LUA( "GroundShockWave", pos, D3DXVECTOR2(100,100), D3DXVECTOR2(1,-1) )
		local pParticle = pMinorParticle:CreateSequence_LUA( "AirDownTick", pos, D3DXVECTOR2(200,200), D3DXVECTOR2(15,-1) )
		if pParticle ~= nil then 
			pParticle:SetLandPosition( pos.y - 5 )
		end
		
		if GetDistance_LUA( pNPCUnit:GetPos(), pX2Game:GetFocusUnitPos_LUA() ) < 1200 then
			pX2Game:GetX2Camera():GetCamera():UpDownCrashCameraNoReset( 10.0, 0.5 )
		end		
	end

end




