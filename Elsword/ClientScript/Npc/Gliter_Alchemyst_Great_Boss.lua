function IsNearLineEndPos( pNPCUnit, distance )
	
	-- if pNPCUnit:GetStateTime() < 0.1 then
		-- return false 
	-- end
	
	local bIsRight = pNPCUnit:GetIsRight()
	local vStartPos = pNPCUnit:GetLineGroupStartPos()
	local vEndPos = pNPCUnit:GetLineGroupEndPos()
	
	if bIsRight == true and pNPCUnit:GetDistanceFrom(vEndPos) < distance then
		return true
   	end
   	
   	if bIsRight == false and pNPCUnit:GetDistanceFrom(vStartPos) < distance then
		return true
    end
    
    return false
	
end
