	end
end

function GLITER_ALCHEMYST_GREAT_BOSS_MAGIC_ATTACK_C_END_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )
	
	if pNPCUnit:AnimEventTimer_LUA( 0.6 ) then
		
		local size = pNPCUnit:GetInt_LUA( 0 )
		--size = size - 1
		local pMajorParticle	= pX2Game:GetMajorParticle()
		
		for i = 0, size do
			
			local pParticle 		= pNPCUnit:GetMajorParticle_LUA( i )
		
			if pParticle ~= nil and pMajorParticle:IsLiveInstance( pParticle ) == true then
				local vPos 			= pParticle:GetPosition()
				local pEffectSet 	= pX2Game:GetEffectSet()
				pEffectSet:PlayEffectSetWithCustomPos_LUA( "EffectSet_Gliter_Alchemyst_Great_Magic_Attack_C_Explosion", pNPCUnit, vPos, D3DXVECTOR3( 0, 0, 0 ) )
				pMajorParticle:DestroyInstance( pParticle )
			end
			
			pNPCUnit:ClearMajorParticle_LUA( i )
		end
		
	end
	
end

