----------------------------------------------------------
function WALLY_8TH_LASER_HELL_JUMP_ATTACK_LANDING_STATE_START( pKTDXApp, pX2Game, pNPCUnit )


	
	WALLY_8TH_LASER_HELL_DOWN_SMOKE( pX2Game, pNPCUnit, 0.0 ) 
	


	local pos		= pNPCUnit:GetPos()
	local landPos = pNPCUnit:GetLandPosition_LUA()
	
	local LHandPos 	= pNPCUnit:GetBonePos_LUA("Bip01_L_Finger0")
	local RHandPos 	= pNPCUnit:GetBonePos_LUA("Bip01_R_Finger0")
	
	
	LHandPos.x = (LHandPos.x + RHandPos.x) * 0.5
	LHandPos.z = (LHandPos.z + RHandPos.z) * 0.5
		
	LHandPos.y = landPos.y
	
	
	local vRot = pNPCUnit:GetRotateDegree()
	if pNPCUnit:GetIsRight() == false then
		vRot.y = vRot.y + 180.0
	end
		
	
	local pDamageEffect = pX2Game:GetDamageEffect()
	pDamageEffect:CreateInstance_LUA2( pNPCUnit, "WALLY_8TH_JUMP_ATTACK_LANDING2", LHandPos, landPos.y+2, vRot )

	local pMinorParticle = pX2Game:GetMinorParticle()
	

	pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "GroundShockWave", LHandPos, D3DXVECTOR2(100,100), D3DXVECTOR2(1,-1) )

	
	local pParticle = pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "Wally8thJumpAttackLandingDebris01", LHandPos, D3DXVECTOR2(200,200), D3DXVECTOR2(3,-1) )
	if pParticle ~= nil then
		pParticle:SetLandPosition( landPos.y + 12 )
	end
	
	pParticle = pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "Wally8thJumpAttackLandingDebris02", LHandPos, D3DXVECTOR2(200,200), D3DXVECTOR2(3,-1) )
	if pParticle ~= nil then
		pParticle:SetLandPosition( landPos.y + 12 )
	end
	
	
	if GetDistance_LUA( pNPCUnit:GetPos(), pX2Game:GetFocusUnitPos_LUA() ) < 1200 then
		pX2Game:GetX2Camera():GetCamera():UpDownCrashCameraNoReset( 15.0, 0.3 )
	end		


end







