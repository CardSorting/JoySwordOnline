	pMajorMeshPlayer:DestroyInstance( pNPCUnit:GetMajorMeshPlayerHandle( 0 ) )
	pMajorMeshPlayer:DestroyInstance( pNPCUnit:GetMajorMeshPlayerHandle( 1 ) )


	pNPCUnit:ClearMajorMeshPlayer_LUA( 0 )
	pNPCUnit:ClearMajorMeshPlayer_LUA( 1 )
	

	
	
end





