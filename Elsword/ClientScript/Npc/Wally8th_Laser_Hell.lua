function WALLY_8TH_LASER_HELL_JUMP_BOOST_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )

	WALLY_8TH_LASER_HELL_CHECK_IF_JUMP_BOOST_DESTROYED( pKTDXApp, pX2Game, pNPCUnit )


	local rotDegree = pNPCUnit:GetRotateDegree()
	local LHandPos 	= pNPCUnit:GetBonePos_LUA("Bip01_L_Finger0")
	local RHandPos 	= pNPCUnit:GetBonePos_LUA("Bip01_R_Finger0")
	
	local pMeshPlayerL = pNPCUnit:GetMajorMeshPlayer_LUA( 0 )
	local pMeshPlayerR = pNPCUnit:GetMajorMeshPlayer_LUA( 1 )
	
	if nil ~= pMeshPlayerL then 
		pMeshPlayerL:SetPos( LHandPos ) 
		pMeshPlayerL:SetRotateDegree( rotDegree )
		pMeshPlayerL:SetMoveAxisAngleDegree( rotDegree ) 
	end
	
	if nil ~= pMeshPlayerR then 
		pMeshPlayerR:SetPos( RHandPos ) 
		pMeshPlayerR:SetRotateDegree( rotDegree )
		pMeshPlayerR:SetMoveAxisAngleDegree( rotDegree ) 
	end
	
end



----------------------------------------------------------
function WALLY_8TH_LASER_HELL_CHECK_IF_JUMP_BOOST_DESTROYED( pKTDXApp, pX2Game, pNPCUnit )

	local pMeshPlayerL = pNPCUnit:GetMajorMeshPlayer_LUA( 0 )
	local pMeshPlayerR = pNPCUnit:GetMajorMeshPlayer_LUA( 1 )
	
	if pMeshPlayerL ~= nil then 
		if false == pX2Game:GetMajorXMeshPlayer():IsLiveInstance(pMeshPlayerL) then 
			pNPCUnit:ClearMajorMeshPlayer_LUA( 0 )
		end
	end
	
	
	if pMeshPlayerR ~= nil then 
	
		if false == pX2Game:GetMajorXMeshPlayer():IsLiveInstance(pMeshPlayerR) then 
			pNPCUnit:ClearMajorMeshPlayer_LUA( 1 )
		end
		
	end
	
	
end





