function CRAZY_PUPPET_SPECIAL_ATTACK_A_STATE_FRAME_MOVE_FUNC( pKTDXApp, pX2Game, pNPCUnit )

	local pMajorParticle = pX2Game:GetMajorParticle()
	local startTime = 2.4
	local vPos = pNPCUnit:GetPos()
	vPos.y = vPos.y + 300
	
	if pNPCUnit:AnimEventTimer_LUA( startTime ) then
		
		local pParticle = pMajorParticle:CreateSequence_LUA( "CRAZY_PUPPET_Countdown", vPos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
		
		if pParticle ~= nil then
			pNPCUnit:SetMajorParticle_LUA( 0, pParticle:GetHandle() ) 
		end
		
		SUMMON_DOLLS( pX2Game, pNPCUnit )
	end
	
	for i = 1, 9 do
	
		if pNPCUnit:EventTimer( i + startTime ) then

			if i == 1 then
				pNPCUnit:SetFlag_LUA( 1, true )		-- 실패 체크
			end
		
			if i == 9 then
				pNPCUnit:SetFlag_LUA( 2, true )		-- 성공 체크
			end
		
			local pParticle = pNPCUnit:GetMajorParticle_LUA( 0 )
			if pParticle ~= nil and pMajorParticle:IsLiveInstance( pParticle ) == true then
				local texName = "PC_" .. 9 - i .. ".dds"
				pParticle:ChangeTexForce_LUA( texName )
			end
		end
	end
	
end

function CRAZY_PUPPET_SPECIAL_ATTACK_END_01_STATE_START_FUNC( pKTDXApp, pX2Game, pNPCUnit )		-- 실패
		
		local numOfNpc = pX2Game:GetNPCUnitListSize()
		
		for i = 0, numOfNpc - 1 do
			local pNpc = pX2Game:GetNPCUnit( i )
			
			if pNpc ~= nil then
				if pNpc:IsUnitID( NPC_UNIT_ID["NUI_CRAZY_PUPPET_SMALL_FALSE"] ) == true or
				   pNpc:IsUnitID( NPC_UNIT_ID["NUI_CRAZY_PUPPET_SMALL_TRUE"] ) == true then
					 
					pNpc:SetNowHP_LUA( 0.0 )
				end
			end
		end
		
		local rateHpDown = pNPCUnit:GetMaxHP() * 0.15
		local nowHp = pNPCUnit:GetNowHP()
		nowHp = nowHp - rateHpDown
		
		if nowHp < 0.0 then
			nowHp = 1.0
		end
		
		pNPCUnit:SetNowHP_LUA( nowHp )
end

