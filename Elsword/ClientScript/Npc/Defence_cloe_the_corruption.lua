function DO_ATTACK(pX2Game, pNPCUnit)
	local iRate = pNPCUnit:GetRandVal() % 100
	
	if iRate < 50 then
		pNPCUnit:StateChange_LUA( "CLOE_THE_CORRUPTION_ATTACK_A" )
	elseif iRate < 90 then
		pNPCUnit:StateChange_LUA( "CLOE_THE_CORRUPTION_ATTACK_B" )
	else
		pNPCUnit:StateChange_LUA( "CLOE_THE_CORRUPTION_SPECIAL_ATTACK_A" )
	end
end












