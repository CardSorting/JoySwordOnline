function JOKE_PPORU_WAIT_CHARGE_READY_STATE_START( pKTDXApp, pX2Game, pNPCUnit )

	if false == pNPCUnit:GetFlag_LUA(RUN_STATE_FLAG) then
		pNPCUnit:SetFlag_LUA(RUN_STATE_FLAG, true)
		InitRunAwayTimer( pKTDXApp, pX2Game, pNPCUnit )					-- 도망 타이머를 준비한다
	end
end


function InitRunAwayTimer( pKTDXApp, pX2Game, pNPCUnit )
	
	pNPCUnit:SetFlag_LUA(RUNAWAY_READY_FLAG, false)  
    
    vec = pNPCUnit:GetPos()
    vec.x = pX2Game:GetGameTime()   	-- 마나를 흡수하는데 성공한 시간을 담아 둔다
    vec.y = 0.0
    vec.z = 0.0
    pNPCUnit:SetVector_LUA(0, vec )
	
end


function Runaway_Timer_Process(pKTDXApp, pX2Game, pNPCUnit)
    
	if pNPCUnit:GetFlag_LUA(RUNAWAY_READY_FLAG) then
		return
	end
	
    if pNPCUnit:GetFlag_LUA(RUN_STATE_FLAG) then
        -- 마나는 이미 흡수했고 이제 도망갈 차례
		vec = pNPCUnit:GetVector_LUA(0)
		PreTime = vec.x
		CurrTime = pX2Game:GetGameTime()   
        ElapsedTime = CurrTime - PreTime
		if ElapsedTime >= RUNAWAY_TIME_AFTER_ABSORB then
			-- 도망갈 시간이 되었다
			pNPCUnit:SetFlag_LUA(RUNAWAY_READY_FLAG, true)
		end
	end
            
end


function JOKE_PPORU_WAIT_CHARGE_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )
	Runaway_Timer_Process(pKTDXApp, pX2Game, pNPCUnit)	
end

function JOKE_PPORU_RUN_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )
	Runaway_Timer_Process(pKTDXApp, pX2Game, pNPCUnit)
	

	if pNPCUnit:AnimEventTimer_LUA( 0.01 ) then
		if pNPCUnit:GetDirPos_LUA(pNPCUnit:GetNearestUserPos()) == 0 then
			pNPCUnit:SetIsRight(true)
		else
			pNPCUnit:SetIsRight(false)
		end
	end
	
end

function JOKE_PPORU_RUNAWAY_READY_STATE_START( pKTDXApp, pX2Game, pNPCUnit )
	pNPCUnit:SetIsRight(false)
end


