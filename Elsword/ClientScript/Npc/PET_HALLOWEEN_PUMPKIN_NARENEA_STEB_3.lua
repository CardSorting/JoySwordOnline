function MovePos( pos, dirvector, dist )
	
	pos.x = pos.x + dist * dirvector.x
	pos.y = pos.y + dist * dirvector.y
	pos.z = pos.z + dist * dirvector.z
	
	return pos
	
end


function PETIT_HALLOWEEN_PUMPKIN_NARENEA_3_ATTACK_FRAME_MOVE( pKTDXApp, pX2Game, pPet )

    if pPet:AnimEventTimer( 0.001 ) then
		local pEffectSet = pX2Game:GetEffectSet()
		if pEffectSet ~= nil then
			pEffectSet:PlayEffectSetByPet_LUA( "EffectSet_PUMPKIN_NARENEA_Start", pPet )
		end
	end

	if pPet:AnimEventTimer( 2.0 ) then
		
		-- 예외 처리
		local pMasterUnit = pPet:GetMaster()				-- 해당 펫 소환자

		if nil == pMasterUnit or 0 >= pMasterUnit:GetNowHP() or nil == pX2Game then
			return
		end
		
		local pDamageEffect = pX2Game:GetDamageEffect()
		
		if nil == pDamageEffect then
			return
		end
		
		-- 여우불 데미지 이펙트
		local vLandPos	 	= pPet:GetLandPosition_LUA()
		local vRot 			= pPet:GetRotateDegree()
		local vDirVector	= pPet:GetDirVector()
		
		local vSummonPos = {}								--공격 목표 위치

		vSummonPos[0] = D3DXVECTOR2(  100,  30 )
		vSummonPos[1] = D3DXVECTOR2(   80,  80 )
		vSummonPos[2] = D3DXVECTOR2(   50, 110 )
		vSummonPos[3] = D3DXVECTOR2(    0, 130 )
		vSummonPos[4] = D3DXVECTOR2(  -50, 110 )
		vSummonPos[5] = D3DXVECTOR2(  -80,  80 )
		vSummonPos[6] = D3DXVECTOR2( -100,  30 )
		
		-- 여우불 7개 발사
		for i = 0, 6 do
		
			local vPos = pPet:GetBonePos_LUA("Dummy_Weapon")
			
			vPos = MovePos( vPos, vDirVector, vSummonPos[i].x )
			
			vPos.y = vPos.y + vSummonPos[i].y
			
			local pFoxFire = pDamageEffect:CreateInstanceByPet_LUA( pMasterUnit, "DAMAGE_EFFECT_PET_NARENEA_ATTACK", vPos, vLandPos, vRot )
			
			if nil ~= pFoxFire then
				pPet:SetLockOnDamageEffect( pFoxFire )		-- 현재 팻이 목표로 정한 유닛을 데미지 이펙트에 록온 시킨다.
			end
		
		end
		
	end

end


function CF_PETATTACK_PETIT_HALLOWEEN_PUMPKIN_NARENEA_3( pKTDXApp, pX2Game, pPet )

	if pX2Game ~= nil then
		if pX2Game:IsHost() == false then
			return false
		end
	end

	if pPet:IsAttacking() == true then

		local fDist = pPet:GetDistTarget()
		if fDist < 300 then
			return true
		end
	end

	return false
end

