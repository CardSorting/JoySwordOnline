function RAVEN_CLONE_DISABLED_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )
    
    if pNPCUnit:AnimEventTimer_LUA( 3.0 ) then    
        
        vecLimtHp = pNPCUnit:GetVector_LUA(0)
        maxLimtHp = vecLimtHp.x
        
        dungeonLv = pX2Game:GetGameLevel()
        liveNpc = pX2Game:LiveNPCNumType_LUA( NPC_UNIT_ID["NUI_RAVEN_CLONE_PROTO"] )	
    	
    	if dungeonLv == 0 then
    	    per = 0.008
    	    
    	elseif dungeonLv == 1 then
    	    per = 0.011
    	    
    	else
    	    per = 0.014
    	    
    	end
   	

    	nowHP = pNPCUnit:GetNowHP_LUA()
    	reFill = pNPCUnit:GetMaxHP() * per
    	setHP = nowHP + reFill    	  	    	
    	    	
    	if setHP >= maxLimtHp then
    	    setHP = maxLimtHp
        end
            	    
        pNPCUnit:SetNowHP_LUA(setHP)    	    	
    	    	
	    if liveNpc < 1 then	    
    		                
	        --vPos = pX2Game:GetLineMap():GetStartPosition( 20 )
