            --pNPCUnit:SetShow(true)                        
	        pNPCUnit:SetPosition(vPos)		            
	        pNPCUnit:StateChange_LUA( "RAVEN_CLONE_WAIT", false )
        	
	        return		        
	    end
	
	    pNPCUnit:ClearEventCheck( 3.0 )
	end
    
