function WATERWAY_CORE_RESPAWNER_START_STATE_START( pKTDXApp, pX2Game, pNPCUnit )

	pNPCUnit:SetInt_LUA( 0, 15 )
	
end


function CF_WATERWAY_CORE_RESPAWNER_RESPAWN( pKTDXApp, pX2Game, pNPCUnit )

	nNPC1		= pX2Game:LiveNPCNumType_LUA( NPC_UNIT_ID["NUI_NASOD_HEALER_BLADER"] )
	nNPC2		= pX2Game:LiveNPCNumType_LUA( NPC_UNIT_ID["NUI_NASOD_ELITE_GUARD"] )
	nNPC3		= pX2Game:LiveNPCNumType_LUA( NPC_UNIT_ID["NUI_NASOD_ELITE_SHERIFF"] )
	nNPC4		= pX2Game:LiveNPCNumType_LUA( NPC_UNIT_ID["NUI_NASOD_SPRAY_ICE"] )
	nNPC5		= pX2Game:LiveNPCNumType_LUA( NPC_UNIT_ID["NUI_WALLY_9TH_TYPE2"] )
	
	nNPC		= nNPC1	+	nNPC2	+	nNPC3	+	nNPC4	+	nNPC5 
		
	if nNPC < pNPCUnit:GetInt_LUA(0) then
		return true
	else
		return false
	end
	
end

function WATERWAY_CORE_RESPAWNER_RESPAWN_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )

	if pNPCUnit:AnimEventTimer_LUA( 0.4 ) then
		pos = pNPCUnit:GetPos()
		
		rand = pNPCUnit:GetRandVal() % 5
		if rand == 0 then
			nNPCID = NPC_UNIT_ID["NUI_NASOD_HEALER_BLADER"]
		elseif rand == 1 then
			nNPCID = NPC_UNIT_ID["NUI_NASOD_ELITE_GUARD"]
		elseif rand == 2 then
			nNPCID = NPC_UNIT_ID["NUI_NASOD_ELITE_SHERIFF"]
		elseif rand == 3 then
			nNPCID = NPC_UNIT_ID["NUI_NASOD_SPRAY_ICE"]
		else
			nNPCID = NPC_UNIT_ID["NUI_WALLY_9TH_TYPE2"]
		end

		pX2Game:CreateNPCReqElite( nNPCID, pNPCUnit:GetHardLevel(), true, pos, false, D3DXVECTOR3(0.0, pNPCUnit:GetKeyCode(), 0), false, false )
			
		if pX2Game:IsHost() then 
			nowHP = pNPCUnit:GetNowHP()			
			pNPCUnit:SetNowHP_LUA( nowHP - 1000.0 )
		end
	end
		
end
