function PET_BLOOD_EATER_CHARGE_FRAME_MOVE( pKTDXApp, pX2Game, pPet )
	
	-- 공격 실행
	if pPet:AnimEventTimer( 0.4 ) then
	
		local pMasterUnit 	= pPet:GetMaster()				--  소환자
		local pDamageEffect = pX2Game:GetDamageEffect()		--	데미지 이펙트 객체
		
		-- 소환자가 없거나 사망 상태이거나 데미지 이펙트 객체를 못 불러 온다면 패스
		if pMasterUnit == nil or pMasterUnit:GetNowHP() <= 0 or pDamageEffect == nil then
			return
		end
		
		local vPos		= pPet:GetBonePos_LUA("Bip01_c")
		local vLandPos	= pPet:GetLandPosition_LUA()
		local vRot		= pPet:GetRotateDegree()
		
		-- 공격 데미지 이펙트 생성
		local pCEffect = pDamageEffect:CreateInstanceByPet_LUA( pMasterUnit, "DAMAGE_EFFECT_PET_BLOOD_EATER_MAGIC_ATTACK", vPos, vLandPos, vRot )
		
		if nil == pCEffect then
			return
		end
		
		local pDamageData = pCEffect:GetDamageData()		-- 공격 이펙트에 대한 데미지 데이터
			
		if nil ~= pDamageData then
			
			pPet:SetDamageData_LUA( pDamageData )				-- 해당 공격 데미지 데이터 저장
		
		end
		
	elseif pPet:AnimEventTimer( 0.8 ) then
	
		-- 공격 명중 검사
		local pDamageData = pPet:GetDamageData_LUA()	-- 저장되어 있는 데미지 데이터
		local pMasterUnit = pPet:GetMaster()			-- 해당 펫의 소환자 유닛

		if nil == pDamageData or nil == pMasterUnit then
			return
		end

		local iHitCount = pPet:GetHitUnitListSize( pDamageData )	-- 피격 수
		
		if 0 >= iHitCount then		-- 맞은 유닛이 없으면, 패스
			return
		end
		
		if 3 < iHitCount then		-- 최대 3마리 제한
			iHitCount = 3
		end
		
		local fIncreaseHP	= pMasterUnit:GetMaxHP() * 0.05				-- 5%의 HP
		
		iHitCount = iHitCount * fIncreaseHP		-- HP 증가치

		local fNowHP = pMasterUnit:GetNowHP() + iHitCount		-- 총 증가할 HP
		local fMaxHP = pMasterUnit:GetMaxHP()					-- 최대 HP
		
		if fMaxHP <= fNowHP then
			fNowHP = fMaxHP
		end

		pMasterUnit:SetNowHP( fNowHP )	-- HP 회복
		
		pPet:ShowIncreaseValue( pMasterUnit:GetPos(), true, iHitCount )	-- 회복치 표시
		
	end
	
end

function CF_PETATTACK_PET_BLOOD_EATER( pKTDXApp, pX2Game, pPet )

	if pX2Game ~= nil then
		if pX2Game:IsHost() == false then
			return false
		end
	end

	if pPet:IsAttacking() == true then

		local fDist = pPet:GetDistTarget()
		if fDist <= 500 then
			return true
		end
	end

	return false
	
end

function MovePos( pos, dirvector, dist )
	
	pos.x = pos.x + dist * dirvector.x
	pos.y = pos.y + dist * dirvector.y
	pos.z = pos.z + dist * dirvector.z
	
	return pos
	
end