function GLITER_NECROMANCER_ATTACK_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )
	
	if pNPCUnit:AnimEventTimer_LUA( 2.020 ) then
		
		pDamageEffect = pX2Game:GetDamageEffect()
		pos = pNPCUnit:GetLandPosition_LUA()		
		--vRotDegree = pNPCUnit:GetRotateDegree()
		
		bIsRight = pNPCUnit:GetIsRight()
		if bIsRight == false then
			pDamageEffect:CreateInstance_LUA( pNPCUnit, "GLITER_NECROMANCER_GHOST_INVERSE", pNPCUnit:GetWeaponBonePos_LUA( 0, "Dummy02" ), pos.y )
		else
			pDamageEffect:CreateInstance_LUA( pNPCUnit, "GLITER_NECROMANCER_GHOST", pNPCUnit:GetWeaponBonePos_LUA( 0, "Dummy02" ), pos.y )
		end
		
		--pDamageEffect:CreateInstance_LUA2( pNPCUnit, "GLITER_NECROMANCER_GHOST", pNPCUnit:GetWeaponBonePos_LUA( 0, "Dummy02" ), pos.y, vRotDegree )
		--pDamageEffect:CreateInstance_LUA( pNPCUnit, "GLITER_NECROMANCER_GHOST", pNPCUnit:GetWeaponBonePos_LUA( 0, "Dummy02" ), pos.y )
	end
	
end



function GLITER_NECROMANCER_DARK_CLOUD_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )

	if pNPCUnit:AnimEventTimer_LUA( 0.500 ) then
		pMinorParticle = pX2Game:GetMinorParticle()
		--pos = pNPCUnit:GetBonePos_LUA("Bip01")
		pos = pNPCUnit:GetLandPosition_LUA()
  		pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "Necromancer_DarkCloud02_UnderLight", pos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
		pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "Necromancer_DarkCloud03_BoosterLight", pos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
 	end


	if pNPCUnit:AnimEventTimer_LUA( 1.573 ) then
		if GetDistance_LUA( pNPCUnit:GetPos(), pX2Game:GetFocusUnitPos_LUA() ) < 1200 then
			pX2Game:GetX2Camera():GetCamera():UpDownCrashCameraNoReset( 5, 0.2 )
		end
        pDamageEffect = pX2Game:GetDamageEffect()
		pos = pNPCUnit:GetLandPosition_LUA()
		pDamageEffect:CreateInstance_LUA( pNPCUnit, "GLITER_NECROMANCER_DARK_CLOUD", pNPCUnit:GetWeaponBonePos_LUA( 0, "Weapon" ), pos.y )
	end
end


