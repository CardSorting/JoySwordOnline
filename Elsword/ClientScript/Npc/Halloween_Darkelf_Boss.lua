function DARKELF_BOSS_DYING_STATE_START( pKTDXApp, pX2Game, pNPCUnit )
	
	pos = pNPCUnit:GetPos()

    	pMajorXMeshPlayer = pX2Game:GetMajorXMeshPlayer()
    	if pMajorXMeshPlayer ~= nil then
        	vPos = pNPCUnit:GetPos()
		rotDegree 	= pNPCUnit:GetRotateDegree()
		
	    	pMajorXMeshPlayer:CreateInstance_LUA( "DarkElf_Boss_Dying_Mesh01", vPos, rotDegree, rotDegree, 14 )
	end




	pos.y = pos.y + 100.0
	GetMinorParticle = pX2Game:GetMinorParticle()
	
	pSeq = GetMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "DieLight",		pos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(3,-1) )
	if pSeq ~= nil then
	
		pSeq:SetLandPosition( pNPCUnit:GetLandPosition_LUA().y )
		pNPCUnit:SetDieSeq( pSeq:GetHandle() )
	
	end
	pNPCUnit:PlaySound_LUA( "DieLight.ogg" )

end


