function LIRE_EVOKE_TRAP_MEMO_MANUAL_TARGET( pKTDXApp, pX2Game, pNPCUnit )

	local iTargetFlag = 2
	local eGameType = pX2Game:GetGameType()
	if eGameType == 1 then
		-- pvp
		iTargetFlag = 0
	else
		-- dungeon
		iTargetFlag = 1
	end

	local pUnit = pX2Game:GetNearestUnit_LUA( pNPCUnit, iTargetFlag )
	
	if pUnit ~= nil then               
         pNPCUnit:SetTargetUnit( pUnit )
	end    
end

