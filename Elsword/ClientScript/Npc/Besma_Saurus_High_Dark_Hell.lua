function SAURUS_HIGH_DARK_HELL_RAGE_START( pKTDXApp, pX2Game, pNPCUnit )

	g_SuperArmorAttack = true

end

-- 헬모드 추가
function SAURUS_HIGH_DARK_HELL_SUPERARMOR_ATTACK_START( pKTDXApp, pX2Game, pNPCUnit )

	if g_SuperArmorAttack == true then
		pNPCUnit:SetSuperArmor(true)
	end
	
end

-- 헬모드 추가
function SAURUS_HIGH_DARK_HELL_SUPERARMOR_ATTACK_END( pKTDXApp, pX2Game, pNPCUnit )
   
	if g_SuperArmorAttack == true then

		local IsNextStage_SuperArmor = false

		for i=1, #SuperArmor_Attack_States do
			if  pNPCUnit:GetNextStateID_LUA() == pNPCUnit:GetStateID_LUA(SuperArmor_Attack_States[i]) then
				IsNextStage_SuperArmor = true
				break
			end	
		end
	
		if IsNextStage_SuperArmor == false then
			pNPCUnit:SetSuperArmor(false)
			g_SuperArmorAttack = false
		end
				
	end

end
