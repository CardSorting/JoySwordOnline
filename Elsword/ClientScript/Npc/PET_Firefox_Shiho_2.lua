function PET_FIREFOX_SHIHO_2_ATTACK_FRAME_MOVE( pKTDXApp, pX2Game, pPet )

	-- 할퀴기 공격
    if pPet:AnimEventTimer( 0.01 ) then

		-- 예외 처리
		local pMasterUnit = pPet:GetMaster()

		if nil == pMasterUnit or 0 >= pMasterUnit:GetNowHP() or nil == pX2Game then
			return
		end
		
		local pEffectSet = pX2Game:GetEffectSet()
		
		if nil == pEffectSet then
			return
		end

		-- 할퀴기 이펙트 셋
		local hEffect = pEffectSet:PlayEffectSetByPet_LUA( "EffectSet_PET_FIREFOX_SHIHO_CROW_ATTACK", pPet )

		if nil ~= hEffect then
			pEffectSet:SetEffectSetInstPos( hEffect, pPet:GetPos(), false )
		end
				
		
	-- 여우불
	elseif pPet:AnimEventTimer( 0.1 ) then
		
		-- 예외 처리
		local pMasterUnit = pPet:GetMaster()				-- 해당 펫 소환자

		if nil == pMasterUnit or 0 >= pMasterUnit:GetNowHP() or nil == pX2Game then
			return
		end
		
		local pDamageEffect = pX2Game:GetDamageEffect()
		
		if nil == pDamageEffect then
			return
		end
		
		-- 여우불 데미지 이펙트
		local vLandPos	 	= pPet:GetLandPosition_LUA()
		local vRot 			= pPet:GetRotateDegree()
		local vDirVector	= pPet:GetDirVector()
		
		local vSummonPos = {}								--공격 목표 위치

		vSummonPos[0] = D3DXVECTOR2(  70, 30 )
		vSummonPos[1] = D3DXVECTOR2(   0, 90 )
		vSummonPos[2] = D3DXVECTOR2( -70, 30 )
		
		-- 여우불 3개 발사
		for i = 0, 2 do
		
			local vPos = pPet:GetBonePos_LUA("Bip01")
			
			vPos = MovePos( vPos, vDirVector, vSummonPos[i].x )
			
			vPos.y = vPos.y + vSummonPos[i].y
			
			local pFoxFire = pDamageEffect:CreateInstanceByPet_LUA( pMasterUnit, "DAMAGE_EFFECT_PET_FIREFOX_SHIHO_FOX_FIRE_2", vPos, vLandPos, vRot )
			
			if nil ~= pFoxFire then
				pPet:SetLockOnDamageEffect( pFoxFire )		-- 현재 팻이 목표로 정한 유닛을 데미지 이펙트에 록온 시킨다.
			end
		
		end
		
	-- 할퀴기 공격 ( 어택 박스 )
    elseif pPet:AnimEventTimer( 0.9 ) then

		-- 예외 처리
		local pMasterUnit = pPet:GetMaster()

		if nil == pMasterUnit or 0 >= pMasterUnit:GetNowHP() or nil == pX2Game then
			return
		end
		
		local pDamageEffect = pX2Game:GetDamageEffect()
		
		if nil == pDamageEffect then
			return
		end
			
		-- 할퀴기 어택 박스용 데미지 이펙트
		local vPos 			= pPet:GetPos()
		local vLandPos	 	= pPet:GetLandPosition_LUA()
		local vRot 			= pPet:GetRotateDegree()
		local vDirVector	= pPet:GetDirVector()
		
		if true == pPet:GetIsRight() then
			vPos = MovePos( vPos, vDirVector, 200 )
		else
			vPos = MovePos( vPos, vDirVector, -200 )
		end

		pDamageEffect:CreateInstanceByPet_LUA( pMasterUnit, "DAMAGE_EFFECT_PET_NINE_TAIL_FOX_CROW_ATTACK_2", vPos, vLandPos, vRot )
		
	end
	
end

function CF_PETATTACK_PET_FIREFOX_SHIHO_2( pKTDXApp, pX2Game, pPet )

	if pX2Game ~= nil then
		if pX2Game:IsHost() == false then
			return false
		end
	end

	if pPet:IsAttacking() == true then

		local fDist = pPet:GetDistTarget()
		if fDist <= 400 then		-- 거리 400
			return true
		end
	end

	return false
	
end

function MovePos( pos, dirvector, dist )
	
	pos.x = pos.x + dist * dirvector.x
	pos.y = pos.y + dist * dirvector.y
	pos.z = pos.z + dist * dirvector.z
	
	return pos
	
end