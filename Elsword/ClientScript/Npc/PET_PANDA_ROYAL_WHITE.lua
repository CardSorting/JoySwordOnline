function PANDA_ROYAL_ATTACK_FRAME_MOVE( pKTDXApp, pX2Game, pPet )

    if pPet:AnimEventTimer( 2.45 ) then

		local pEffectSet = pX2Game:GetEffectSet()
		if pEffectSet ~= nil then
		
			local strEffectName = "EffectSet_Spirit_Panda_1"
			local iEffectIndex = pPet:GetRandomInt() % 4 + 1

			if iEffectIndex == 2 then
				strEffectName = "EffectSet_Spirit_Panda_2"
			elseif iEffectIndex == 3 then
				strEffectName = "EffectSet_Spirit_Panda_3"
			elseif iEffectIndex == 4 then
				strEffectName = "EffectSet_Spirit_Panda_4"
			end
		
			local hEffect =  pEffectSet:PlayEffectSetByPet_LUA( strEffectName, pPet )
			if iEffectIndex == 4 then
				local pMasterUnit = pPet:GetMaster()
				pEffectSet:SetEffectSetInstPos( hEffect, pMasterUnit:GetPos(), true )
			else 
				pEffectSet:SetEffectSetInstPos( hEffect, pX2Game:GetNearestNPCUnitPos_LUA( pPet:GetPos(), 0, 10000 ), true )	
			end			
		end
		
	end

end

function CF_PETATTACK_PANDA_ROYAL( pKTDXApp, pX2Game, pPet )

	if pX2Game ~= nil then
		if pX2Game:IsHost() == false then
			return false
		end
	end

	if pPet:IsAttacking() == true then
		local fDist = pPet:GetDistTarget()
		if fDist <= 500 then
			return true
		end
	end

	return false
	
end