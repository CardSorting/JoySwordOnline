function CF_NASOD_MOUSEHOUSE_RESPAWN( pKTDXApp, pX2Game, pNPCUnit )
	
	nNPC = pX2Game:LiveNPCNumType_LUA( NPC_UNIT_ID["NUI_NASOD_MOUSE"] )
		
	if nNPC <= 3 then
		return true
	else
		return false
	end
	
end

function NASOD_MOUSEHOUSE_RESPAWN_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )

	if pNPCUnit:AnimEventTimer_LUA( 0.4 ) then		
		pos = pNPCUnit:GetPos()
				
		pX2Game:CreateNPCReq_LUA( NPC_UNIT_ID["NUI_NASOD_MOUSE"], pNPCUnit:GetHardLevel(), TRUE, pos, pNPCUnit:GetIsRight(), 0, true  )
		
		if pX2Game:IsHost() then 
			nowHP = pNPCUnit:GetNowHP()			
			pNPCUnit:SetNowHP_LUA( nowHP - 500.0 )
		end
	end
		
end


function NASOD_MOUSEHOUSE_DYING_STATE_START( pKTDXApp, pX2Game, pNPCUnit )

--	pNPCUnit:PlaySound_LUA( "Nasod_MouseHouse_Broken.ogg" )
	pMinorParticle = pX2Game:GetMinorParticle()
	pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "DownSmoke", pNPCUnit:GetLandPosition_LUA(), D3DXVECTOR2(100,100), D3DXVECTOR2(7,-1) )

end
