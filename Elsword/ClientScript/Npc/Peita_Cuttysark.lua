function CF_CUTTYSARK_FLY_STANDUP( pKTDXApp, pX2Game, pNPCUnit )
	local fStateTime = pNPCUnit:GetStateTime()
	local nDownHitCount = pNPCUnit:GetInt_LUA(0)

	if nDownHitCount == 0 and fStateTime > 4 then
		return true
	end
	if nDownHitCount == 1 and fStateTime > 4 then
		return true
	end
	if nDownHitCount == 2 and fStateTime > 5 then
		return true
	end
	if nDownHitCount == 3 and fStateTime > 6 then
		return true
	end
	if nDownHitCount == 4 and fStateTime > 7 then
		return true
	end

	if fStateTime > 10 then
		return true
	end

	return false
end

