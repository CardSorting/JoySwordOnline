function CUTTYSARK_DAMAGE_STATE_START( pKTDXApp, pX2Game, pNPCUnit )

    --print("CUTTYSARK_DAMAGE")

    --pNPCUnit:SetUserHold(false, "")
	pNPCUnit:SetUserHoldReset()
    if pNPCUnit:GetNowHP() <= 0 then	    
	     pNPCUnit:StateChange_LUA("CUTTYSARK_DAMAGE_AIR_FALL_READY", true)
	     return
	end

    local pAttackerGameUnit = pNPCUnit:GetLastAttackGameUnit()
    if pAttackerGameUnit ~= nil and pAttackerGameUnit:GetGameUnitType() == 1 then
        local pAttacker = pNPCUnit:GetLastAttackUnit()    
        
        if pAttacker ~= nil then
            local nDownHitCount = pNPCUnit:GetInt_LUA(0)
            local nHited = pNPCUnit:GetInt_LUA(1)			
            --print("Attacker Air Down : ", nHited)
                    
            if pAttacker:IsUnitID( NPC_UNIT_ID["NUI_TRAP_CROSSBOW"] ) == true then
				 nHited = nHited + 1
				if nDownHitCount == 0 then
					pNPCUnit:StateChange_LUA("CUTTYSARK_DAMAGE_AIR_FALL_READY", true)
					pNPCUnit:SetInt_LUA(1, 0)
					return
				end
				
                if nDownHitCount ~= 0 and nHited >= nDownHitCount then
                    nHited = 0
                    pNPCUnit:StateChange_LUA("CUTTYSARK_DAMAGE_AIR_FALL_READY", true)
                else                    
                    pNPCUnit:StateChange_LUA("CUTTYSARK_DAMAGE_AIR", true)
                end
                
                pNPCUnit:SetInt_LUA(1, nHited)
                
                return
            end        
        end	
    end
end

