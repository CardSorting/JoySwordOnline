function ENT_SMALL_ATTACK_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )

	if pNPCUnit:AnimEventTimer_LUA( 1.085 ) then
		
		pNPCUnit:ClearHitUnitList_LUA()
		pNPCUnit:SetDamageData_LUA( "DAMAGE_DATA_LAST" )
		
	end

end


function ENT_SMALL_ATTACK_RISING_TREE_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )


	for i=0, 3 do 

		fTime = 0.768 + i * 0.1
		
		if pNPCUnit:AnimEventTimer_LUA( fTime ) then
		
			landPos = pNPCUnit:GetLandPosition_LUA()
			dirVector = pNPCUnit:GetDirVector()
			bIsRight = pNPCUnit:GetIsRight()
			
			
			if true == bIsRight then 
				landPos.x = landPos.x + dirVector.x * 150.0 * (i+1)
				landPos.z = landPos.z + dirVector.z * 150.0 * (i+1)
			else
				landPos.x = landPos.x - dirVector.x * 150.0 * (i+1)
				landPos.z = landPos.z - dirVector.z * 150.0 * (i+1)
			end
			
			landPos = pX2Game:GetLineMap():GetLandPosition_LUA( landPos )
			
			pDamageEffect = pX2Game:GetDamageEffect()
			
			if pDamageEffect ~= nil then
			    pDamageEffect:CreateInstance_LUA( pNPCUnit, "ENT_SMALL_SPIKE_BOARD", landPos, landPos.y )
            end		
		end

	end
	

end



