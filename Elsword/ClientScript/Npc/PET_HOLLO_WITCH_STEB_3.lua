function HOLLO_WITCH_STEB_3_ATTACK_FRAME_MOVE( pKTDXApp, pX2Game, pPet )

    if pPet:AnimEventTimer( 0.3 ) then
		local pDamageEffect = pX2Game:GetDamageEffect()

		if pDamageEffect ~= nil then
			local vLandPos = pPet:GetLandPosition_LUA()
			local vPos = pPet:GetBonePos_LUA("Bip01_R_Hand")
			local vRot = pPet:GetRotateDegree()
			
			vPos.y = vPos.y - 50
			
	        local pMasterUnit = pPet:GetMaster()
	        if pMasterUnit ~= nil and pMasterUnit:GetNowHP() > 0 then
				pDamageEffect:CreateInstanceByPet_LUA( pMasterUnit, "PET_HOLLOWITCH_ATTACK3", vPos, vLandPos, vRot )
			end
		end
	end
end


function CF_PETATTACK_HOLLO_WITCH_STEB_3( pKTDXApp, pX2Game, pPet )

	if pX2Game ~= nil then
		if pX2Game:IsHost() == false then
			return false
		end
	end

	if pPet:IsAttacking() == true then

		local fDist = pPet:GetDistTarget()
		if fDist <= 500 then
			return true
		end
	end

	return false
end