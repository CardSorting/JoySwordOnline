function LIZARDMAN_HIGH_DARK_EXPERT_COMBOA2_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )
	
	
	
	-- wind effect framemove
	pWind = pNPCUnit:GetMajorMeshPlayer_LUA( 0 ) 
	if nil ~= pWind then 
	
		pMajorXMeshPlayer = pX2Game:GetMajorXMeshPlayer()		
		if false == pMajorXMeshPlayer:IsLiveInstance(pWind) then 
			pNPCUnit:ClearMajorMeshPlayer_LUA( 0 )
		else
			vPos		= pNPCUnit:GetPos()
			rotDegree 	= pNPCUnit:GetRotateDegree()
			
			pWind:SetPos( vPos ) 
			pWind:SetRotateDegree( rotDegree )
			pWind:SetMoveAxisAngleDegree( rotDegree ) 
		end
		
	end
		

	-- wind effect 생성
	if pNPCUnit:AnimEventTimer_LUA( 0.9 ) then
		
		vPos		= pNPCUnit:GetPos()
		rotDegree 	= pNPCUnit:GetRotateDegree()
			
		pMajorXMeshPlayer = pX2Game:GetMajorXMeshPlayer()		
		pWind = pMajorXMeshPlayer:CreateInstance_LUA( "LizardManHighComboA2", vPos, rotDegree, rotDegree, 14 )
		
		pNPCUnit:SetMajorMeshPlayer_LUA( 0, pWind )
	
	end
	
		
	if pNPCUnit:AnimEventTimer_LUA( 0.8 ) then
		pNPCUnit:ClearHitUnitList_LUA()
		pNPCUnit:SetDamageData_LUA( "DAMAGE_DATA_NEXT" )
	end
	
	if pNPCUnit:AnimEventTimer_LUA( 1.12 ) then
		pNPCUnit:ClearHitUnitList_LUA()
	end
		
	if pNPCUnit:AnimEventTimer_LUA( 1.19 ) then
		pNPCUnit:ClearHitUnitList_LUA()
	end
	
		
	if pNPCUnit:AnimEventTimer_LUA( 1.27 ) then
		pNPCUnit:ClearHitUnitList_LUA()
	end
		
		
	if pNPCUnit:AnimEventTimer_LUA( 1.35 ) then
		pNPCUnit:ClearHitUnitList_LUA()
		pNPCUnit:SetDamageData_LUA( "DAMAGE_DATA_LAST" )
	end

end





