function NASOD_HEALER_BLADER_P_COMMON_HEAL_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )

	
	if pNPCUnit:AnimEventTimer_LUA( 0.43 ) then
	
		vPos = pNPCUnit:GetBonePos_LUA( "Bone_Jewelry" )
		pMinorParticle = pX2Game:GetMinorParticle()	
		pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "TimeStop1", vPos, D3DXVECTOR2(100,100), D3DXVECTOR2(1,-1) )
	
	end
	
	if pNPCUnit:AnimEventTimer_LUA( 0.63 ) then
	
		pMajorParticle = pX2Game:GetMajorParticle()	
		
		vPos = pNPCUnit:GetBonePos_LUA( "Bone_Jewelry" )
		fDist = 700.0
		nCount = 0
		nUnitListSize = pX2Game:GetNPCUnitListSize()
				
		for i=0, nUnitListSize-1 do 
			
			pUnit = pX2Game:GetNPCUnit( i )			  
			    
			if 	pUnit ~= nil and
			    pUnit:GetUID() ~= pNPCUnit:GetUID() and
				pUnit:GetTeam() == pNPCUnit:GetTeam() and				
				pUnit:GetDistanceFrom(vPos) < fDist and
				pUnit:GetNowHP() < pUnit:GetMaxHP() and	
				pUnit:GetNowHP() > 0 and							
				pUnit:IsUnitID(NPC_UNIT_ID["NUI_NASOD_KING"]) ~= true and
				pUnit:IsUnitID(NPC_UNIT_ID["NUI_NASOD_KING_HENIR"]) ~= true and
			    	pUnit:IsUnitID(NPC_UNIT_ID["NUI_EL_POWER_PLANT"]) ~= true and
				pUnit:IsUnitID(NPC_UNIT_ID["NUI_EL_POWER_PLANT_HENIR"]) ~= true and
			    	pUnit:IsUnitID(NPC_UNIT_ID["NUI_EL_EXTRACTOR_FIRE"]) ~= true and
			    	pUnit:IsUnitID(NPC_UNIT_ID["NUI_EL_EXTRACTOR_ICE"]) ~= true and
			    	pUnit:IsUnitID(NPC_UNIT_ID["NUI_EL_EXTRACTOR_EARTH"]) ~= true and
			    	pUnit:IsUnitID(NPC_UNIT_ID["NUI_EL_EXTRACTOR_NOVA"]) ~= true then			    
				
				pMajorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "Heal_Effect01", pUnit:GetPos(), D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
				pMajorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "LobbyLevelUp01", pUnit:GetPos(), D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
				
				--healedHP = pUnit:GetNowHP() + 5000.0 * pNPCUnit:GetHardLevel()
				healedHP = pUnit:GetNowHP() + pUnit:GetMaxHP() * 0.2 -- 최대 HP의 20% healing
				if healedHP > pUnit:GetMaxHP() then
					
					healedHP = pUnit:GetMaxHP()
				
				end
				
				pUnit:SetNowHP_LUA( healedHP )
								
			end
				
		end
		
	end

end




