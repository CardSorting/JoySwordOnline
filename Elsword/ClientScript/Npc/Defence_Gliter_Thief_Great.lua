function MovePos( pos, dirvector, dist )
	
	pos.x = pos.x + dist * dirvector.x
	pos.y = pos.y + dist * dirvector.y
	pos.z = pos.z + dist * dirvector.z
	
	return pos
	
end

function GLITER_THIEF_GREAT_MANUAL_TARGET( pKTDXApp, pX2Game, pNPCUnit )
	
