function GLITER_THIEF_GREAT_DO_ATTACK(pX2Game, pNPCUnit)
	local iRate = pNPCUnit:GetRandVal() % 100
	
	if iRate < 30 then
		pNPCUnit:StateChange_LUA( "GLITER_THIEF_GREAT_ATTACK_B_READY" )
	elseif iRate < 70 then
		pNPCUnit:StateChange_LUA( "GLITER_THIEF_GREAT_ATTACK_B_READY" )
	else
		pNPCUnit:StateChange_LUA( "GLITER_THIEF_GREAT_ATTACK_B_READY" )
	end
