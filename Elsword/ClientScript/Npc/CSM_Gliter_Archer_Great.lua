function CSM_GLITER_ARCHER_GREAT_SELF_COMMAND_TRANSIT_STATE_START( pKTDXApp, pX2Game, pNPCUnit )


	--pNPCUnit:SetPrevAIMessage_LUA( STATE_CHANGE_TYPE["SCT_NO_CHANGE"] )

	if pNPCUnit:GetGroupAICommand_LUA() == GROUP_AI_COMMAND["GAI_CMD_FORCED_MOVE"] then


		pUnit = pNPCUnit:GetOnlyConsultedNPC_LUA( NPC_UNIT_ID["NUI_GLITER_COMMANDER"] )
		if pUnit == nil then
			return
		end

		if pUnit:GetFlag_LUA(6) == true or pUnit:GetFlag_LUA(7) == true or pUnit:GetFlag_LUA(8) == true or pUnit:GetFlag_LUA(9) == true then
			pUID = pX2Game:GetNearestUserUnitUID_LUA( pNPCUnit:GetPos(), 0, 999999 )

			if pUID ~= nil then
				pNPCUnit:COMMAND_ATTACK_MOVE_LUA( GROUP_AI_TARGET_TYPE["GAI_TT_USER"], pUID) 
				
			end
			return
		else
			pUID = pX2Game:GetNearestUserUnitUID_LUA( pNPCUnit:GetPos(), 0, 999999 )

			if pUID ~= nil then
				pNPCUnit:COMMAND_SKILL_LUA( GROUP_AI_COMMAND["GAI_CMD_SKILL_C"], GROUP_AI_TARGET_TYPE["GAI_TT_USER"], pUID) 
			end
			return
		end
	end

	if pNPCUnit:GetGroupAICommand_LUA() == GROUP_AI_COMMAND["GAI_CMD_SKILL_A"] then

		pUID = pX2Game:GetNearestUserUnitUID_LUA( pNPCUnit:GetPos(), 0, 999999 )

		if pUID ~= nil then
			pNPCUnit:COMMAND_ATTACK_MOVE_LUA( GROUP_AI_TARGET_TYPE["GAI_TT_USER"], pUID) 
		end
		return
	end

end

