function GIANT_PPORU_EVENT_SILER_WAIT_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )
	
	if pNPCUnit:GetDamagedThisFrame() then 
		fRemainHPRate = pNPCUnit:GetNowHP() / pNPCUnit:GetMaxHP()
		if fRemainHPRate < 0.7 then	
			pNPCUnit:CallOtherNPC( NPC_UNIT_ID["NUI_BEE"] )
			pNPCUnit:CallOtherNPC( NPC_UNIT_ID["NUI_MONKEY_A"] )
			pNPCUnit:CallOtherNPC( NPC_UNIT_ID["NUI_MONKEY_B"] )
		end
	end	

end 

