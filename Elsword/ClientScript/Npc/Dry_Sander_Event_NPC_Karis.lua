function DRY_SANDER_EVENT_NPC_KARIS_DYING_START_START( pKTDXApp, pX2Game, pNPCUnit )
		
	-- 이벤트신 끝나면 실제 왈도를 활성화 시킴
	local pBossNpc = pX2Game:GetNPCUnitByType( NPC_UNIT_ID["NUI_WALDO_TROCK"] )
	
	if pBossNpc ~= nil then
		pBossNpc:StateChange_LUA( "WALDO_TROCK_START" )
	end
	
	pNPCUnit:SetPositionForce( D3DXVECTOR3( 0.0, -5000, 0.0 ) )
	
end