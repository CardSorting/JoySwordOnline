function GLITER_ALCHEMYST_SECRET_BOSS_MAGIC_ATTACK_B_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )

	if pNPCUnit:AnimEventTimer_LUA( 1.23 ) then
		local numUser = pX2Game:GetUserUnitNum_LUA()
		local numLiveUser = pX2Game:LiveUserUnitNum()		
		if numLiveUser > 0 then
			local pEffectSet = pX2Game:GetEffectSet()			
			local extraDamage = { "CURSE", "POISON", "REVERSE", "FIRE", "SLOW" }

			local numMagicAttackB = 1
			if numLiveUser >= 2 and numLiveUser <= 3 then
				numMagicAttackB = 2
			elseif numLiveUser >= 4 then
				numMagicAttackB = 3
			end

			local nUserInx = pNPCUnit:GetRandVal(0) % numUser			

			local nEffect = 1	
			while nEffect <= numMagicAttackB do
				local pTargetUser = pX2Game:GetUserUnit(nUserInx)
				if pTargetUser ~= nil and pTargetUser:GetNowHP() > 0.0 then
					local num = pNPCUnit:GetRandVal( numUser + nEffect ) % 5 + 1
					local effectsetName = "Effectset_ALCHEMYST_SECRET_BOSS_MAGIC_ATTACK_B_" .. extraDamage[num]			
					
					local vPos = pTargetUser:GetLandPosition_LUA()
					pEffectSet:PlayEffectSetWithCustomPos_LUA( effectsetName, pNPCUnit, vPos, D3DXVECTOR3( 0, 0, 0 ) )

					nEffect = nEffect + 1
				end

				nUserInx = nUserInx + 1
				if nUserInx >= numUser then
					nUserInx = 0
				end
			end

		end
	end
end

