	local maxHp = pNPCUnit:GetMaxHP()
	local nowHp = pNPCUnit:GetNowHP_LUA()

	local bOnLine = pNPCUnit:IsOnSomething_LUA()

	if bOnLine == true and false == bIsChangedGameUnitScale then
		if pNPCUnit:GetFlag_LUA(3) == false and (nowHp / maxHp) < 0.8 then
			pNPCUnit:SetFlag_LUA(3, true)
