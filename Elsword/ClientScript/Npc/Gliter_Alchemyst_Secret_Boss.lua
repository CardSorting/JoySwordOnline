function CF_GLITER_ALCHEMYST_SECRET_BOSS_MAGIC_ATTACK_C( pKTDXApp, pX2Game, pNPCUnit )

	local bIsChangedGameUnitScale = pNPCUnit:IsChangedGameUnitScaleByBuff()
	
	local maxHp = pNPCUnit:GetMaxHP()
	local nowHp = pNPCUnit:GetNowHP_LUA()

	local bOnLine = pNPCUnit:IsOnSomething_LUA()

	if bOnLine == true and false == bIsChangedGameUnitScale  then
		if pNPCUnit:GetFlag_LUA(0) == false and (nowHp / maxHp) < 0.7 then
			pNPCUnit:SetFlag_LUA(0, true)
			return true
		elseif pNPCUnit:GetFlag_LUA(1) == false and (nowHp / maxHp) < 0.4 then
			pNPCUnit:SetFlag_LUA(1, true)
			return true
		elseif pNPCUnit:GetFlag_LUA(2) == false and (nowHp / maxHp) < 0.1 then
			pNPCUnit:SetFlag_LUA(2, true)
			return true
		-- elseif pNPCUnit:GetFlag_LUA(2) == false and (nowHp / maxHp) < 0.2 then
			-- pNPCUnit:SetFlag_LUA(2, true)
			-- return true	
		else
			return false
		end
	else
		return false
	end
end

