function CF_SUMMON_SHADOW_BLAST_BOMB( pKTDXApp, pX2Game, pNPCUnit )
    
	local pUnit = pX2Game:GetNearestUnit_LUA( pNPCUnit, 2 )
	if pUnit ~= nil then
		local nearDist =  pX2Game:GetDist_LUA( pNPCUnit:GetPos(), pUnit:GetPos() )
		if nearDist <= 300 then
			return true
		end
	end

	return false
end

function SUMMON_SHADOW_BLAST_STATE_START( pKTDXApp, pX2Game, pNPCUnit )

    pNPCUnit:SetAIEnable( false )
	pNPCUnit:StartSelfDestruction( 20 )
end

function SUMMON_SHADOW_BLAST_BOMB_STATE_END( pKTDXApp, pX2Game, pNPCUnit )
	pNPCUnit:SetNowHP_LUA(0)
end

function SUMMON_SHADOW_BLAST_DYING_STATE_START( pKTDXApp, pX2Game, pNPCUnit )
    
    local pDamageEffect = pX2Game:GetDamageEffect()
    local landPos = pNPCUnit:GetLandPosition_LUA()
    local vPos = pNPCUnit:GetPos()    
    
    if pDamageEffect ~= nil then
		pDamageEffect:CreateInstance_LUA( pNPCUnit, "SUMMON_SHADOW_BLAST_EXPLOSION", vPos, landPos.y)
			
	    pNPCUnit:SetShow(false)
    end			
end
