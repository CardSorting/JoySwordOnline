function PETTE_PPORU_XMAS2010_STEB_3_ATTACK_FRAME_MOVE( pKTDXApp, pX2Game, pPet )

    if pPet:AnimEventTimer( 0.25 ) then
		local pDamageEffect = pX2Game:GetDamageEffect()

		if pDamageEffect ~= nil then
			local vLandPos = pPet:GetLandPosition_LUA()
			local vPos = pPet:GetBonePos_LUA("Bip01_R_Hand")
			local vRot = pPet:GetRotateDegree()
			local vPos1 = vPos
			local vPos2 = vPos
	        vPos1.y = vPos1.y + 35
	        vPos2.y = vPos2.y + 50

	        local pMasterUnit = pPet:GetMaster()
	        if pMasterUnit ~= nil and pMasterUnit:GetNowHP() > 0 then
				pDamageEffect:CreateInstanceByPet_LUA( pMasterUnit, "PET_PETTE_PPORU_XMAS_ATTACK1", vPos1, vLandPos, vRot )
				pDamageEffect:CreateInstanceByPet_LUA( pMasterUnit, "PET_PETTE_PPORU_XMAS_ATTACK2", vPos2, vLandPos, vRot )
				pDamageEffect:CreateInstanceByPet_LUA( pMasterUnit, "PET_PETTE_PPORU_XMAS_ATTACK3", vPos, vLandPos, vRot )
			end
		end
	end
end


function CF_PETATTACK_PETTE_PPORU_XMAS2010_STEB_3( pKTDXApp, pX2Game, pPet )

	if pX2Game ~= nil then
		if pX2Game:IsHost() == false then
			return false
		end
	end

	if pPet:IsAttacking() == true then

		local fDist = pPet:GetDistTarget()
		if fDist <= 300 then
			return true
		end
	end

	return false
end