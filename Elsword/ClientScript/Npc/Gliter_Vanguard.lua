function GLITER_VANGUARD_OVER_B_SPECIAL_ATTACK_A_STATE_START_FUNC( pKTDXApp, pX2Game, pNPCUnit )
	pNPCUnit:SetInt_LUA( 1, 0 )		-- 1번 인덱스에 0이라는 값을 저장( 0이라는 값은 레이저를 쏠 위치를 지정할 인덱스로 쓰일 것임 )
	
	local XPosLazer = { -750, -450, -150, 150, 450, 750 }
	local tableForShuffle = {}
	
	for i, v in ipairs( XPosLazer ) do
		tableForShuffle[i] = { density = pNPCUnit:GetRandVal(i), val = v }
	end
	
	table.sort( tableForShuffle, START_POS_COMP )
	
	for i, v in ipairs( tableForShuffle ) do
		pNPCUnit:SetVector_LUA( i - 1, D3DXVECTOR3( (i - 1) * 0.23, v.val, 0 ) )			-- 0.3초 간격으로 v.val의 위치에 생성
	end	
	
end

function GLITER_VANGUARD_OVER_B_SPECIAL_ATTACK_A_STATE_FRAME_MOVE_FUNC( pKTDXApp, pX2Game, pNPCUnit )

	local index = pNPCUnit:GetInt_LUA( 1 )
	
	if index >= 6 then
		return;
	end
	
	local vec	= pNPCUnit:GetVector_LUA( index )	
	if pNPCUnit:AnimEventTimer_LUA( 2.82 + vec.x ) then
		local pEffectSet 	= pX2Game:GetEffectSet()
		local pos 			= pNPCUnit:GetPos()
		local landPos 		= pNPCUnit:GetLandPosition_LUA()
			
		pos.x = pos.x + vec.y		-- 레이저가 떨어질 X좌표 위치
		pEffectSet:PlayEffectSetWithCustomPos_LUA( "EffectSet_Gliter_Vanguard_Over_B_Special_Attack_A_Finish", pNPCUnit, pos, D3DXVECTOR3( 0, 0, 0 ))
		
		index = index + 1
		pNPCUnit:SetInt_LUA( 1, index )
	end
	
end

