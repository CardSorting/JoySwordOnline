function GLITER_COMMON_FRAME_FUNC( pKTDXApp, pX2Game, pNPCUnit )
	
	local pArcherNpc = pX2Game:GetNPCUnitByType( NPC_UNIT_ID["NUI_GLITER_ARCHER_GREAT_TRAP"] )
	
	if pArcherNpc == nil and pNPCUnit:GetTimerElapsedTime(0) > 180.0 then
		pNPCUnit:SetTimerRestart( 0 )
		pX2Game:CreateNPCReq_LUA( NPC_UNIT_ID["NUI_GLITER_ARCHER_GREAT_TRAP"], pNPCUnit:GetHardLevel(), false, D3DXVECTOR3( -2138.09, 1583, 5786.34 ), false, 0.3, true  )
		pX2Game:CreateNPCReq_LUA( NPC_UNIT_ID["NUI_GLITER_ARCHER_GREAT_TRAP"], pNPCUnit:GetHardLevel(), false, D3DXVECTOR3( -1568.02, 1583, 5786.78 ), false, 0.3, true  )
		pX2Game:CreateNPCReq_LUA( NPC_UNIT_ID["NUI_GLITER_ARCHER_GREAT_TRAP"], pNPCUnit:GetHardLevel(), false, D3DXVECTOR3( 1560.04, 1583, 5789.21 ), false, 0.3, true  )
		pX2Game:CreateNPCReq_LUA( NPC_UNIT_ID["NUI_GLITER_ARCHER_GREAT_TRAP"], pNPCUnit:GetHardLevel(), false, D3DXVECTOR3( 2136.6, 1583, 5789.66 ), false, 0.3, true  )
	end
	
	if pNPCUnit:GetInt_LUA( 0 ) == 3 then
		return
	end

	local maxHP = pNPCUnit:GetMaxHP()
	
	if pNPCUnit:GetNowHP() < maxHP * 0.2 then
		pNPCUnit:SetNowHP_LUA( maxHP )
		
		if pNPCUnit:GetInt_LUA( 0 ) == 1 then
			pNPCUnit:SetInt_LUA( 0, 2 )		-- 상태 2로 변경
			pNPCUnit:StateChange_LUA( "GLITER_VANGUARD_OVER_A_READY", false )
		elseif pNPCUnit:GetInt_LUA( 0 ) == 2 then
			pNPCUnit:SetInt_LUA( 0, 3 )		-- 상태 2로 변경
			pNPCUnit:StateChange_LUA( "GLITER_VANGUARD_OVER_B_READY", false )
		end
		
	end
