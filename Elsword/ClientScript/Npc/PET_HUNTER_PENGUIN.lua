function PETIT_HUNTER_PENGUIN_ATTACK_FRAME_MOVE( pKTDXApp, pX2Game, pPet )

	local pEffectSet = pX2Game:GetEffectSet()
	if pEffectSet ~= nil then
		if pPet:AnimEventTimer( 0.2 ) then
			local pTargetUnit = pPet:GetTargetUnit()
			if pTargetUnit == nil then
				return
			end
			
			local pMasterUnit = pPet:GetMaster()

			local vDir = pTargetUnit:GetPos()
			local vPetPos = pPet:GetPos()
			vDir.x = vDir.x - vPetPos.x
			vDir.z = vDir.z - vPetPos.z
			vDir.y = 0
			
			--락온1
			local hEffect = pEffectSet:PlayEffectSetTraceTarget_LUA( "EffectSet_PengZZang_Attack_C_Lockon", pMasterUnit, pTargetUnit )
			pPet:SetInt_LUA( 0, hEffect )

			--락온2
			pTargetUnit = pX2Game:GetNearestNPCUnitOnSameDirection_LUA( pTargetUnit:GetPos(), 0, 1000, vDir )
			if pTargetUnit == nil then
				pTargetUnit = pPet:GetTargetUnit()
			end
			hEffect = pEffectSet:PlayEffectSetTraceTarget_LUA( "EffectSet_PengZZang_Attack_C_Lockon", pMasterUnit, pTargetUnit )
			pPet:SetInt_LUA( 1, hEffect )

			--락온3
			pTargetUnit = pX2Game:GetNearestNPCUnitOnSameDirection_LUA( pTargetUnit:GetPos(), 0, 1000, vDir )
			if pTargetUnit == nil then
				pTargetUnit = pPet:GetTargetUnit()
			end
			hEffect = pEffectSet:PlayEffectSetTraceTarget_LUA( "EffectSet_PengZZang_Attack_C_Lockon", pMasterUnit, pTargetUnit )
			pPet:SetInt_LUA( 2, hEffect )
		elseif pPet:AnimEventTimer( 0.567 ) then
			--총구이펙트1
			pEffectSet:PlayEffectSetByPet_LUA( "EffectSet_PengZZang_Attack_C", pPet )
			--펑1
			local hOldEffect = pPet:GetInt_LUA( 0 )
			local hEffect = pEffectSet:PlayEffectSetByPet_LUA( "EffectSet_PengZZang_Attack_C_Explosion", pPet )
			pEffectSet:SetEffectSetInstPos( hEffect, pEffectSet:GetEffectPosition_LUA( hOldEffect ), true )
			pEffectSet:StopEffectSet_LUA( hOldEffect )
		elseif pPet:AnimEventTimer( 1.000 ) then
			--총구이펙트2
			pEffectSet:PlayEffectSetByPet_LUA( "EffectSet_PengZZang_Attack_C", pPet )
			--펑2
			local hOldEffect = pPet:GetInt_LUA( 1 )
			local hEffect = pEffectSet:PlayEffectSetByPet_LUA( "EffectSet_PengZZang_Attack_C_Explosion", pPet )
			pEffectSet:SetEffectSetInstPos( hEffect, pEffectSet:GetEffectPosition_LUA( hOldEffect ), true )
			pEffectSet:StopEffectSet_LUA( hOldEffect )
		elseif pPet:AnimEventTimer( 1.433 ) then
			--총구이펙트3
			pEffectSet:PlayEffectSetByPet_LUA( "EffectSet_PengZZang_Attack_C", pPet )
			--펑3
			local hOldEffect = pPet:GetInt_LUA( 2 )
			local hEffect = pEffectSet:PlayEffectSetByPet_LUA( "EffectSet_PengZZang_Attack_C_Explosion", pPet )
			pEffectSet:SetEffectSetInstPos( hEffect, pEffectSet:GetEffectPosition_LUA( hOldEffect ), true )
			pEffectSet:StopEffectSet_LUA( hOldEffect )
		end
	end

end

function CF_PETATTACK_PETIT_HUNTER_PENGUIN( pKTDXApp, pX2Game, pPet )

	if pX2Game ~= nil then
		if pX2Game:IsHost() == false then
			return false
		end
	end

	if pPet:IsAttacking() == true then

		local fDist = pPet:GetDistTarget()
		if fDist < 300 then
			return true
		end
	end

	return false
end
