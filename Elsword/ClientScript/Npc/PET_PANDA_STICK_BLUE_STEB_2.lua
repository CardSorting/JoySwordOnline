function PANDA_STICK_BLUE_STEB_2_ATTACK_FRAME_MOVE( pKTDXApp, pX2Game, pPet )

    if pPet:AnimEventTimer( 1.0 ) then
		local pDamageEffect = pX2Game:GetDamageEffect()

		if pDamageEffect ~= nil then
			local vLandPos = pPet:GetLandPosition_LUA()
			local vPos = pPet:GetBonePos_LUA("Bip01_L_Hand")
			local vRot = pPet:GetRotateDegree()
			vPos.y = vPos.y + 35

	        local pMasterUnit = pPet:GetMaster()
	        if pMasterUnit ~= nil and pMasterUnit:GetNowHP() > 0 then
				pDamageEffect:CreateInstanceByPet_LUA( pMasterUnit, "PET_PANDA_STICK_BLUE_ATTACK_MONSTER02", vLandPos, vLandPos, vRot )

			end
		end

	end
	
end

function CF_PETATTACK_PANDA_STICK_BLUE_STEB_2( pKTDXApp, pX2Game, pPet )

	if pX2Game ~= nil then
		if pX2Game:IsHost() == false then
			return false
		end
	end

	if pPet:IsAttacking() == true then

		local fDist = pPet:GetDistTarget()
		if fDist <= 300 then
			return true
		end
	end

	return false
	
end