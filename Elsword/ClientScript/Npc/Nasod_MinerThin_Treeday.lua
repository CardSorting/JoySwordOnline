		pParticle = pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "Smoke_Nasod_MinerThin_Attack", vBonePos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
		if pParticle ~= nil then
