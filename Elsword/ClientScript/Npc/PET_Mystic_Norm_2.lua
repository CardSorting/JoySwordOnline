function MYSTIC_NORM_2_ATTACK_FRAME_MOVE( pKTDXApp, pX2Game, pPet )

    if pPet:AnimEventTimer( 0.001 ) then
		local pEffectSet = pX2Game:GetEffectSet()
		if pEffectSet ~= nil then
			pEffectSet:PlayEffectSetByPet_LUA( "EffectSet_Pet_Mystic_Attack2", pPet )
		end
	end

	if pPet:AnimEventTimer( 0.68 ) then
		local pDamageEffect = pX2Game:GetDamageEffect()

		if pDamageEffect ~= nil then
			local vLandPos = pPet:GetLandPosition_LUA()
			local vPos = pPet:GetBonePos_LUA("Attack_Dummy02")
			local vRot1 = pPet:GetRotateDegree()
			local vRot2 = pPet:GetRotateDegree()

			vRot1.z = vRot1.z + 20
			vRot1.y = vRot1.y + 60
			vRot2.z = vRot2.z + 20
			vRot2.y = vRot2.y - 60

	        local pMasterUnit = pPet:GetMaster()
	        if pMasterUnit ~= nil and pMasterUnit:GetNowHP() > 0 then
				local pPetMissile1 = pDamageEffect:CreateInstanceByPet_LUA( pMasterUnit, "DAMAGE_EFFECT_PET_MYSTIC_MISSILE", vPos, vLandPos, vRot1 )
				local pPetMissile2 = pDamageEffect:CreateInstanceByPet_LUA( pMasterUnit, "DAMAGE_EFFECT_PET_MYSTIC_MISSILE", vPos, vLandPos, vRot2 )
				pMasterUnit:SetLockOnNearTarget_LUA( pPetMissile1, 0 )
				pMasterUnit:SetLockOnNearTarget_LUA( pPetMissile2, 1 )
			end
		end
	end
end

function CF_PETATTACK_MYSTIC_NORM_2( pKTDXApp, pX2Game, pPet )

	if pX2Game ~= nil then
		if pX2Game:IsHost() == false then
			return false
		end
	end

	if pPet:IsAttacking() == true then

		local fDist = pPet:GetDistTarget()
		if fDist <= 99999 then
			return true
		end
	end

	return false
end

