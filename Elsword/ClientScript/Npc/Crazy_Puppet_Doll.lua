function CRAZY_PUPPET_DOLL_MANUAL_TARGET( pKTDXApp, pX2Game, pNPCUnit )
         		
    local pUnit = pX2Game:GetNPCUnitByType( NPC_UNIT_ID["NUI_CRAZY_PUPPET_NOT_MOVE"] )
	local pUnit1 = pX2Game:GetNPCUnitByType( NPC_UNIT_ID["NUI_CRAZY_PUPPET_NOT_MOVE_HELL"] )
		
	local iRandUnit = pNPCUnit:GetRandVal() % 2
	if iRandUnit == 0 and pUnit ~= nil then
		
			pNPCUnit:SetTargetUnit( pUnit )
		
	elseif pUnit1 ~= nil then
		
			pNPCUnit:SetTargetUnit( pUnit1 )
		
	end
end

