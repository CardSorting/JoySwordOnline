function CRAZY_PUPPET_DOLL_START_STATE_START( pKTDXApp, pX2Game, pNPCUnit )
	local vPos = pNPCUnit:GetPos()
	local rotDegree = pNPCUnit:GetRotateDegree()
	local pMinorXMeshPlayer = pX2Game:GetMinorXMeshPlayer()
	pMinorXMeshPlayer:CreateInstance_LUA( "RespawnLanding01", vPos, rotDegree, rotDegree, 14 )
	pMinorXMeshPlayer:CreateInstance_LUA( "RespawnLanding02", vPos, rotDegree, rotDegree, 14 )

	local  minorParticle = pX2Game:GetMinorParticle()
	minorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "RespawnLanding_P01", pNPCUnit:GetPos(), D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )	
end

function CRAZY_PUPPET_DOLL_MERGER_STATE_START( pKTDXApp, pX2Game, pNPCUnit )

	local pUnit1 = pX2Game:GetNPCUnitByType( NPC_UNIT_ID["NUI_CRAZY_PUPPET_NOT_MOVE"] )
	local pUnit2 = pX2Game:GetNPCUnitByType( NPC_UNIT_ID["NUI_CRAZY_PUPPET_NOT_MOVE_HELL"] )
    
	local fDist1 = 99999
	local fDist2 = 99999

	if pUnit1 ~= nil then
		fDist1 = GetDistance_LUA( pNPCUnit:GetPos(), pUnit1:GetPos() )
	end
	if pUnit2 ~= nil then
		fDist2 = GetDistance_LUA( pNPCUnit:GetPos(), pUnit2:GetPos() )
	end
    
	if fDist1 < fDist2 then
		if pUnit1 ~= nil then 
			local numUser = pX2Game:GetUserUnitNum_LUA()
			local fUpRate = 0.05
			if numUser > 1 then
				fUpRate = 0.1
			end
			local fHp = pUnit1:GetNowHP_LUA()
			local fUpHp = pUnit1:GetMaxHP() * 0.1
			pUnit1:SetNowHP_LUA( fHp + fUpHp ) 		
		end
	else
		if pUnit2 ~= nil then 
			local numUser = pX2Game:GetUserUnitNum_LUA()
			local fUpRate = 0.05
			if numUser > 1 then
				fUpRate = 0.1
			end
			local fHp = pUnit2:GetNowHP_LUA()
			local fUpHp = pUnit2:GetMaxHP() * 0.1
			pUnit2:SetNowHP_LUA( fHp + fUpHp ) 		
		end
	end
	
end

function CRAZY_PUPPET_DOLL_MERGER_STATE_END( pKTDXApp, pX2Game, pNPCUnit )
	pNPCUnit:SetNowHP_LUA(0)
end

function CF_CRAZY_PUPPET_DOLL_WIN( pKTDXApp, pX2Game, pNPCUnit )

	if pX2Game:LiveUserUnitNum() == 0 then
		return true
	else
		return false
	end

end

function CF_CRAZY_PUPPET_DOLL_BEFORE_EXPLOSION( pKTDXApp, pX2Game, pNPCUnit )
  
  local userPos = pNPCUnit:GetNearestUserPos()
  local fDist = pNPCUnit:GetDistanceFrom(userPos)

	if fDist <= 300 then	    	    
	    return true
	end
	
	return false
end

function CF_CRAZY_PUPPET_DOLL_MERGER( pKTDXApp, pX2Game, pNPCUnit )

  local pUnit = pX2Game:GetNPCUnitByType( NPC_UNIT_ID["NUI_CRAZY_PUPPET_NOT_MOVE"] )
  if pUnit ~= nil then
    local fDist = GetDistance_LUA( pNPCUnit:GetPos(), pUnit:GetPos() )
    if fDist <= 300 then
      return true   
    end 
  end

  pUnit = pX2Game:GetNPCUnitByType( NPC_UNIT_ID["NUI_CRAZY_PUPPET_NOT_MOVE_HELL"] )
  if pUnit ~= nil then
    local fDist = GetDistance_LUA( pNPCUnit:GetPos(), pUnit:GetPos() )
    if fDist <= 300 then
      return true   
    end 
  end
    
  return false
end


