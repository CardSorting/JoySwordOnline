-- 조건 체크
function CF_GOD_MAGICIAN_GLITER_NECRO_EVENT_SPECIAL( pKTDXApp, pX2Game, pNPCUnit )
	-- by hcsung( 저사양에서 매프레임 for문 돌면서 계산하는 오버헤드 발생하여 아래의 조건문 추가함 )
	local fElapsedTime = pNPCUnit:GetTimerElapsedTime( 0 )
	if fElapsedTime > 0.1 then
		pNPCUnit:SetTimerRestart(0)
		local dist = 800
		
		local sizeOfNpcUnit = pX2Game:GetNPCUnitListSize()
		local vPos = pNPCUnit:GetBonePos_LUA( "Bip01_Head" )
		
		for i = 0, sizeOfNpcUnit - 1 do
			local pNpc = pX2Game:GetNPCUnit(i)
			
			if  pNpc ~= nil and
				pNpc:GetDistanceFrom( vPos ) < dist and
				pNpc:GetNowHP() < pNpc:GetMaxHP() and
				pNpc:GetNowHP() > 0 and
				pNpc:GetTeam() == pNPCUnit:GetTeam() and
				pNpc:GetUID()  ~= pNPCUnit:GetUID() then
				
				return true			
			end
		end
	end
	return false
end

