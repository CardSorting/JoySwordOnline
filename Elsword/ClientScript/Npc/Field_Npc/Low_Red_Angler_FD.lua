--FunctionName : LOW_RED_ANGLER_FD_ATTACK_FRAME_MOVE
function LOW_RED_ANGLER_FD_ATTACK_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )
	if pNPCUnit:AnimEventTimer_LUA( 0.9 ) then
		pNPCUnit:ClearHitUnitList_LUA()
		pNPCUnit:SetDamageData_LUA( "DAMAGE_DATA_SECOND" )
		pNPCUnit:SetSpeedX( INIT_PHYSIC["RUN_SPEED"] )
		
	elseif pNPCUnit:AnimEventTimer_LUA( 1.5 ) then
		pNPCUnit:ClearHitUnitList_LUA()
		pNPCUnit:SetDamageData_LUA( "DAMAGE_DATA_LAST" )
		pNPCUnit:SetSpeedX( INIT_PHYSIC["RUN_SPEED"] )
	end
end



