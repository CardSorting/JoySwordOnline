	local ToTheRight = true
	local StartPos = pNPCUnit:GetPos()
	local TargetPos = pX2Game:GetNearestUserUnitPos_LUA( pNPCUnit:GetPos(), 0, 999999 )

	if pNPCUnit:IsRightTarget_LUA(pNPCUnit, TargetPos) == true then
		ToTheRight = false
	else
		ToTheRight = true
	end

--[[
	IsRight = pUnit1:GetIsRight()

	if IsRight == true then
		IsRight = false

	else
	

	IsRight = true
	end
--]]
	local Distance = 50

	local pUnit1 = pNPCUnit:GetOnlyConsultNPC_LUA( NPC_UNIT_ID["NUI_GLITER_SHIELD_GREAT"] )
	if pUnit1 ~= nil then

		StartPos = pUnit1:GetPos()
		TargetPos = pX2Game:GetNearestUserUnitPos_LUA( pUnit1:GetPos(), 0, 999999 )

		if pNPCUnit:IsRightTarget_LUA(pUnit1, TargetPos) == true then
			ToTheRight = false
		else
			ToTheRight = true
		end

		local Pos = pX2Game:GetFixedDistancePosition_LUA( StartPos , Distance, ToTheRight )
		Distance = Distance + 150
		pUnit1:COMMAND_FORCED_MOVE_GROUND_LUA(Pos)

	end



	local pUnit2 = pNPCUnit:GetOnlyConsultNPC_LUA( NPC_UNIT_ID["NUI_GLITER_LANCE_GREAT"] )
	if pUnit2 ~= nil then

		local Pos = pX2Game:GetFixedDistancePosition_LUA( StartPos , Distance, ToTheRight )
		Distance = Distance + 150
		pUnit2:COMMAND_FORCED_MOVE_GROUND_LUA(Pos)

	end



	local pUnit4 = pNPCUnit:GetOnlyConsultNPC_LUA( NPC_UNIT_ID["NUI_GLITER_HAMMER_GREAT"] )
	if pUnit4 ~= nil then

		local Pos = pX2Game:GetFixedDistancePosition_LUA( StartPos , Distance, ToTheRight )
		Distance = Distance + 150
		pUnit4:COMMAND_FORCED_MOVE_GROUND_LUA(Pos)

	end


	local pUnit3 = pNPCUnit:GetOnlyConsultNPC_LUA( NPC_UNIT_ID["NUI_GLITER_ARCHER_GREAT"] )
	if pUnit3 ~= nil then

		local Pos = pX2Game:GetFixedDistancePosition_LUA( StartPos , Distance, ToTheRight )
		Distance = Distance + 150
		pUnit3:COMMAND_FORCED_MOVE_GROUND_LUA(Pos)

