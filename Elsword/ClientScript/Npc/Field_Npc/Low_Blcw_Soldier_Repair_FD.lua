function CF_LOW_BLCW_SOLDIER_REPAIR_FD_HEAL( pKTDXApp, pX2Game, pNPCUnit )
    
	local vPos = pNPCUnit:GetLandPosition_LUA()
	--fDist = 350.0
	--nCount = 0
	local nUnitListSize = pX2Game:GetNPCUnitListSize()
		
	
	local pUID = pX2Game:GetNearNPCUnitUID_LUA( pNPCUnit:GetPos(), NPC_UNIT_ID["NUI_CANNON_BIG"], 350 )		
	local pUID2 = pX2Game:GetNearNPCUnitUID_LUA( pNPCUnit:GetPos(), NPC_UNIT_ID["NUI_CANNON_HEAVY_MACHINE_GUN"], 350 )	
		
	if pUID ~= 0 then	
	    for i=0, nUnitListSize-1 do     		
		    local pUnit = pX2Game:GetNPCUnit( i )
    		
		    if pUnit ~= nil then
		        if pUnit:GetUID() == pUID and pUnit:GetNowHP() > 0 and
                    pUnit:GetUID() ~= pNPCUnit:GetUID() and
			        pUnit:GetTeam() == pNPCUnit:GetTeam() and				
			        --pUnit:GetDistanceFrom(vPos) < fDist and			
			        pUnit:GetNowHP() <= pUnit:GetMaxHP() * 0.3 then            		
			        		       
                    
			        --pNPCUnit:SetIsRight(pNPCUnit:GetDirPos_LUA(pUnit:GetPos()))
			        
                	
                	pNPCUnit:SetInt_LUA(0, i)
                	
			        return true                                
		        end		            
		    end	        		            
        end	                     
    end		        
    
    if pUID2 ~= 0 then	
	    for i=0, nUnitListSize-1 do     		
		    local pUnit = pX2Game:GetNPCUnit( i )
    		
		    if pUnit ~= nil then
		        if pUnit:GetUID() == pUID2 and pUnit:GetNowHP() > 0 and		                
                    pUnit:GetUID() ~= pNPCUnit:GetUID() and
			        pUnit:GetTeam() == pNPCUnit:GetTeam() and				
			        --pUnit:GetDistanceFrom(vPos) < fDist and			
			        pUnit:GetNowHP() <= pUnit:GetMaxHP() * 0.3 then            		
			                            
			        --pNPCUnit:SetIsRight(pNPCUnit:GetDirPos_LUA(pUnit:GetPos()))

                                    	
                	
                	pNPCUnit:SetInt_LUA(0, i)
                	
			        return true                                
		        end		            
		    end	        		            
        end	                     
    end		        
    
	return false

end

