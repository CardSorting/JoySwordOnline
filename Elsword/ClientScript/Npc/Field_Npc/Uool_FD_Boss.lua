function UOOL_FD_BOSS_COMMON_FRAMEMOVE_FUNC( pKTDXApp, pX2Game, pNPCUnit )
	if nil ~= pNPCUnit then
		local fRemainHPRate = pNPCUnit:GetNowHP() / pNPCUnit:GetMaxHP()
		local iFatFightingPhase = pNPCUnit:GetInt_LUA ( 0 ) 
		
		if fRemainHPRate < 0.10 then 
			if iFatFightingPhase == 2 then
				local pNpc = pX2Game:GetNearestNpcInSpecificRangeByNpcId_LUA( pNPCUnit:GetPos(), NPC_UNIT_ID["NUI_ILLY_FD_BOSS"], 999999 )
				if pNpc ~= nil then	
					CallAnotherUnit ( pNPCUnit, pNpc, 200.0, 3 )
				end
			end
			
		elseif fRemainHPRate < 0.35 then 
			if iFatFightingPhase == 1 then
				local pNpc = pX2Game:GetNearestNpcInSpecificRangeByNpcId_LUA( pNPCUnit:GetPos(), NPC_UNIT_ID["NUI_ILLY_FD_BOSS"], 999999 )
				if pNpc ~= nil then	
					CallAnotherUnit ( pNPCUnit, pNpc, 200.0, 2 )
				end		
			end
	
		elseif fRemainHPRate < 0.75 then 
			if iFatFightingPhase == 0 then
				local pNpc = pX2Game:GetNearestNpcInSpecificRangeByNpcId_LUA( pNPCUnit:GetPos(), NPC_UNIT_ID["NUI_ILLY_FD_BOSS"], 999999 )
				if pNpc ~= nil then	
					CallAnotherUnit ( pNPCUnit, pNpc, 200.0, 1 )
				end
			end
		end
	end
end
	

