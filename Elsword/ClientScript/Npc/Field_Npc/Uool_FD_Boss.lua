function CallAnotherUnit ( pNPCUnit, pNpc, fAddPosY, iPhase )		
	if pNPCUnit == nil then
		return 
	end
	if pNpc == nil then
		return
	end
	pNPCUnit:SetInt_LUA ( 0, iPhase )
	local pNpcLocalPosition = pNPCUnit:GetLandPosition_LUA()
	local dest_local_x = pNpcLocalPosition.x + fAddPosY
	pNpcLocalPosition.x = dest_local_x	
	pNpc:SetPosition ( pNpcLocalPosition )
	local pNpcOriginPosition = pNpc:GetLandPosition_LUA()
	
	if pNpcOriginPosition.y ~= pNpcLocalPosition.y then
		if fAddPosY < 0 then
			CallAnotherUnit ( pNPCUnit, pNpc, (fAddPosY / 2 * -1 ), iPhase  )
		else
			CallAnotherUnit ( pNPCUnit, pNpc, (fAddPosY * -1), iPhase )
			return
		end			
	end
	
	if fAddPosY > 0 then
		pNPCUnit:SetIsRight ( true ) 
		pNpc:SetIsRight ( false )	
	else
		pNPCUnit:SetIsRight ( false ) 
		pNpc:SetIsRight ( true )
	end
		
	
	pNPCUnit:SetBuffFactorToGameUnitByBuffFactorID_LUA( BUFF_FACTOR_ID["BFI_BUFF_ANGER_OF_UOOL"] )	
	pNPCUnit:StateChange_LUA( "UOOL_FD_BOSS_FIGHTING_POSE_BEFORE", true )
	pNpc:StateChange_LUA ( "SOLDIER_THIN_CHEER_UP_BEFORE", true ) 
	
end



