function ELDER_SOLDIER_BIG_JUMP_ATTACK_A_LANDING_STATE_START( pKTDXApp, pX2Game, pNPCUnit )

	local pos = pNPCUnit:GetPos()
	local landPos = pNPCUnit:GetLandPosition_LUA()
	
	local vRot = pNPCUnit:GetRotateDegree()
	if pNPCUnit:GetIsRight() == false then
		vRot.y = vRot.y + 180.0
	end
	
	local pDamageEffect = pX2Game:GetDamageEffect()
	pDamageEffect:CreateInstance_LUA2( pNPCUnit, "ELDER_SOLDIER_BIG_JUMP_ATTACK_A_LANDING", pos, landPos.y+2, vRot )

	pNPCUnit:PlaySound_LUA( "Down.ogg" )
	local pMinorParticle = pX2Game:GetMinorParticle()
	
	local pParticle = pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "SoldierFatJumpAttackADebris01", pos, D3DXVECTOR2(200,200), D3DXVECTOR2(3,-1) )
	if pParticle ~= nil then
		pParticle:SetLandPosition( landPos.y + 12 )
	end
	
	pParticle = pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "SoldierFatJumpAttackADebris02", pos, D3DXVECTOR2(200,200), D3DXVECTOR2(3,-1) )
	if pParticle ~= nil then
		pParticle:SetLandPosition( landPos.y + 12 )
	end

end



