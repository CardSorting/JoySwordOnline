function HAECH_ICE_3_ATTACK_FRAME_MOVE( pKTDXApp, pX2Game, pPet )


	if pPet:AnimEventTimer( 0.80 ) then
		local pEffectSet = pX2Game:GetEffectSet()
		if pEffectSet ~= nil then
			pEffectSet:PlayEffectSetByPet_LUA( "EffectSet_Pet_Hach_Pre_Fire", pPet )
		end

		local pDamageEffect = pX2Game:GetDamageEffect()

		if pDamageEffect ~= nil then

	        local pMasterUnit = pPet:GetMaster()
	        if pMasterUnit ~= nil and pMasterUnit:GetNowHP() > 0 then

				local vLandPos = pPet:GetLandPosition_LUA()
				local vTargetPos = pX2Game:GetNearestNPCUnitPos_LUA(pMasterUnit:GetPos(), 0, 999999)
				vTargetPos.y = vTargetPos.y + 80
				local vFirePos = pPet:GetBonePos_LUA("Mouse_01")
				local vDirFire = D3DXVECTOR3(vTargetPos.x - vFirePos.x, vTargetPos.y - vFirePos.y, vTargetPos.z - vFirePos.z)
				local vFireDegree = GetDirVecToDegree( vDirFire )
				pPetMissile1 = pDamageEffect:CreateInstanceByPet_LUA( pMasterUnit, "DAMAGE_EFFECT_PET_HACH_FIREBALL_ICE3", vFirePos, vLandPos, vFireDegree )
			end
		end
	end
end

function CF_HAECH_ICE_3_ATTACK( pKTDXApp, pX2Game, pPet )

	if pX2Game ~= nil then
		if pX2Game:IsHost() == false then
			return false
		end
	end

	if pPet:IsAttacking() == true then

		local fDist = pPet:GetDistTarget()
		if fDist <= 2000 then
			return true
		end
	end

	return false
end

function HAECH_ICE_3_SPECIAL_FRAME_MOVE( pKTDXApp, pX2Game, pPet )

    if pPet:AnimEventTimer( 0.001 ) then
		local pEffectSet = pPet:GetUIEffectSet()
		if pEffectSet ~= nil then
			pEffectSet:PlayEffectSetByPet_LUA( "EffectSet_Pet_Hach_Sigh", pPet )
		end
    end
end

function CF_HAECH_ICE_3_LANDING_FAIL( pKTDXApp, pX2Game, pPet )

	if pPet:GetNowStateTimer() > 2.5 then
		return true
	end
	return false
end