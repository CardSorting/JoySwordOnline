function MYSTIC_BLK_1_ATTACK_FRAME_MOVE( pKTDXApp, pX2Game, pPet )

    if pPet:AnimEventTimer( 0.001 ) then
		local pEffectSet = pX2Game:GetEffectSet()
		if pEffectSet ~= nil then
			pEffectSet:PlayEffectSetByPet_LUA( "EffectSet_Pet_Mystic_Attack1", pPet )
		end
	end

	if pPet:AnimEventTimer( 0.515 ) then
		local pDamageEffect = pX2Game:GetDamageEffect()

		if pDamageEffect ~= nil then
			local vLandPos = pPet:GetLandPosition_LUA()
			local vPos = pPet:GetBonePos_LUA("ATTACK_Dummy02")
			local vRot = pPet:GetRotateDegree()

			--vRot.z = vRot.z + 20
			--vRot.y = vRot.y + 60

	        local pMasterUnit = pPet:GetMaster()
	        if pMasterUnit ~= nil and pMasterUnit:GetNowHP() > 0 then
				local pPetMissile = pDamageEffect:CreateInstanceByPet_LUA( pMasterUnit, "DAMAGE_EFFECT_PET_MYSTIC_MISSILE", vPos, vLandPos, vRot )
				pMasterUnit:SetLockOnNearTarget_LUA( pPetMissile, 0 )
			end
		end
	end

end

function CF_PETATTACK_MYSTIC_BLK_1( pKTDXApp, pX2Game, pPet )

	if pX2Game ~= nil then
		if pX2Game:IsHost() == false then
			return false
		end
	end

	if pPet:IsAttacking() == true then

		local fDist = pPet:GetDistTarget()
		if fDist <= 99999 then
			return true
		end
	end

	return false
end
