function HENIR_CLEAR_GLAVE_START_STATE_START( pKTDXApp, pX2Game, pNPCUnit )
    local fHenirElapsedTime = pX2Game:GetGameTime()   
	if fHenirElapsedTime  < 720.0 then
		pNPCUnit:SetFlag_LUA(1, true) 
	elseif fHenirElapsedTime  < 1500.0 then
		pNPCUnit:SetFlag_LUA(2, true)
	elseif fHenirElapsedTime  < 2400.0 then
		pNPCUnit:SetFlag_LUA(3, true) 
	else
		pNPCUnit:SetFlag_LUA(3, true) 
	end
end
