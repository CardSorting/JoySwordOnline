function CRAZY_PUPPET_SMALL_FALSE_START_STATE_START_FUNC( pKTDXApp, pX2Game, pNPCUnit )
	
		local pos = pNPCUnit:GetPos()
		pos.x = ( pos.x % 10 ) / 10
		pNPCUnit:SetVector_LUA( 0, pos )
		pNPCUnit:SetTimerRestart( 0 )
	
end

function CRAZY_PUPPET_SMALL_FALSE_WALK_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )
	
	if pNPCUnit:AnimEventTimer_LUA( 0.01 ) then
		local randNum = pNPCUnit:GetRandVal() % 2
		
		if randNum == 0 then
			pNPCUnit:SetIsRight( true )
		else
			pNPCUnit:SetIsRight( false )
		end
	end
		
end

function CRAZY_PUPPET_SMALL_FALSE_DASH_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )
	
	if pNPCUnit:AnimEventTimer_LUA( 0.01 ) then
		local randNum = pNPCUnit:GetRandVal() % 2
		
		if randNum == 0 then
			pNPCUnit:SetIsRight( true )
		else
			pNPCUnit:SetIsRight( false )
		end
	end
		
end

----------- condition function----------------------------------------------------------
----------------------------------------------------------------------------------------
function CF_CRAZY_PUPPET_SMALL_FALSE_WAIT( pKTDXApp, pX2Game, pNPCUnit )
	
	local vec = pNPCUnit:GetVector_LUA( 0 )
	
	if pNPCUnit:GetTimerElapsedTime( 0 ) < vec.x then
		return false
	else
		return true
	end
end

function CF_CRAZY_PUPPET_SMALL_FALSE_WIN( pKTDXApp, pX2Game, pNPCUnit )

	if pX2Game:LiveUserUnitNum() == 0 then
		return true
	else
		return false
	end

end

