----------------------------------------------------------------------------------------

function ChangeDirByRandom( pNPCUnit )
	
	local randNum = pNPCUnit:GetRandVal() % 2
		
	if randNum == 0 then
		pNPCUnit:SetIsRight( true )
	else
		pNPCUnit:SetIsRight( false )
	end
	
end
