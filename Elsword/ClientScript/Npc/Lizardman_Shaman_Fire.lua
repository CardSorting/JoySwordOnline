function LIZARDMAN_SHAMAN_FIRE_CHARGE_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )


	if pNPCUnit:AnimEventTimer_LUA( 0.01 ) then

		vPos = pNPCUnit:GetPos()
		vBonePos = pNPCUnit:GetWeaponBonePos_LUA( 0, "TRACE_START0" )
		vBonePos.y = vBonePos.y + 140.0

		pMinorParticle = pX2Game:GetMinorParticle()
		pCharge1 = pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "LizardMan_Shaman_Charge01", vPos,	D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,1000) )
		pCharge2 = pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "LizardMan_Shaman_Charge02", vBonePos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,1000) )
		pCharge3 = pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "LizardMan_Shaman_Charge03", vPos,	D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,1000) )
		
		if pCharge1 ~= nil then 
			pNPCUnit:SetMinorParticle_LUA( 0, pCharge1:GetHandle() ) 
		end 
		
		if pCharge2 ~= nil then 
			pNPCUnit:SetMinorParticle_LUA( 1, pCharge2:GetHandle() ) 
		end 
		
		if pCharge3 ~= nil then 
			pNPCUnit:SetMinorParticle_LUA( 2, pCharge3:GetHandle() ) 
		end 
	end



	if pNPCUnit:AnimEventTimer_LUA( 0.4 ) then
		pNPCUnit:PlaySound_LUA( "LizardMan_Charge.ogg" )
		fMP = pNPCUnit:GetNowMP() + 10.0					
		if fMP > pNPCUnit:GetMaxMP() then
			fMP = pNPCUnit:GetMaxMP() 
		end
		
		pNPCUnit:SetNowMP( fMP )		
		pNPCUnit:ClearEventCheck( 0.4 )
		
	end
	
end


