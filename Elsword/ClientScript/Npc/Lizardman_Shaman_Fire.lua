	end
	
	if pCharge2 ~= nil then
		
		pMinorParticle:DestroyInstance( pCharge2 )
		pNPCUnit:ClearMinorParticle_LUA( 1 )
		
	end
	
	
	if pCharge3 ~= nil then
		
		pMinorParticle:DestroyInstance( pCharge3 )
		pNPCUnit:ClearMinorParticle_LUA( 2 )
		
	end
	
end





