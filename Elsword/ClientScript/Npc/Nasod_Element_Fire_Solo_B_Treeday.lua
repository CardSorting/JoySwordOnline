function NASOD_ELEMENT_FIRE_TELEPORT_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )
    
    if pNPCUnit:AnimEventTimer_LUA( 0.8 ) then
        nUnitListSize = pX2Game:GetNPCUnitListSize()    	
    	
	    pUID = pX2Game:GetNearNPCUnitUID_LUA( pNPCUnit:GetPos(), NPC_UNIT_ID["NUI_BARRIERMACHINE_FIRE"], 999999 )
    	
	    if pUID ~= 0 then
    	
	        for i=0, nUnitListSize-1 do 
        		
		        pUnit = pX2Game:GetNPCUnit( i )
        		
		        if pUnit ~= nil then
		            if pUnit:GetUID() == pUID then    		
	                    --pNPCUnit:SetShow(false)
	                    vSummonPos = pX2Game:GetLineMap():GetStartPosition( 5 )	   
	                    vSummonPos.y = vSummonPos.y + 150                 
	                    pNPCUnit:SetPosition(vSummonPos, true) 
                        return
                    end
                end	            
            end	             
        end		            
    end
end

