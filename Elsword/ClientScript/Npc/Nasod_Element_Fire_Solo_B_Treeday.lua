		    if pUnit:IsUnitID(NPC_UNIT_ID["NUI_BARRIERMACHINE_FIRE"]) == true and pUnit:GetFlag_LUA(3) == true then
		        bLiveBarrierMachineIce = true
		        break
		    end
        end 
    end

    if bLiveBarrierMachineIce == true then
        pNPCUnit:SetSuperArmor(true)
    end        



    bDamage = pNPCUnit:GetDamagedThisFrame()        
    if bDamage == true and bLiveBarrierMachineIce == true then
        
        --pNPCUnit:SetSuperArmor(true)                    
        pMajorParticle1 = pX2Game:GetMajorParticle() 
                
        if pMajorParticle1 ~= nil then
                    
            vPos = pNPCUnit:GetPos()
            vPos.y = vPos.y + 180                        
            vRotateDegree	= pNPCUnit:GetRotateDegree()                
            
            if pNPCUnit:GetAttackFront() == false then
                vRotateDegree.y = vRotateDegree.y + 180
            end
                        
                        
            pParticle1 = pNPCUnit:GetMajorParticle_LUA(1)                                            
            
            if pParticle1 ~= nil then
                if pMajorParticle1:IsLiveInstance( pParticle1 ) == true then
                    		
		            pParticle1:SetPosition( vPos ) 
        		    
		            pParticle1:SetAddRotate( vRotateDegree )
		            pParticle1:SetAxisAngle( vRotateDegree )	   
		        else
		            pNPCUnit:ClearMajorParticle_LUA( 1 )
		        end
        		
            else  
                                    
                pParticle1 = pMajorParticle1:GameUnitCreateSequence_LUA( pNPCUnit, "Count_Barrier01", vPos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(1,-1) )
                if pParticle1 ~= nil then 
                
					pParticle1:SetAddRotate( vRotateDegree )
					pParticle1:SetAxisAngle( vRotateDegree )
					pNPCUnit:SetMajorParticle_LUA(1, pParticle1:GetHandle() )
				end 
            end                                                


            pNPCUnit:PlaySound_LUA( "Nasod_Element_BarrierHit.ogg" )
            -- 배리어 효과음(?)
            
        end
        
    end
end

