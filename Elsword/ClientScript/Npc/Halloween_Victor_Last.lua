	local vPos = pNPCUnit:GetPos()
	
	for i = 0, userListSize do
		local pUser = pX2Game:GetUserUnit( i )
		if pUser ~= nil then
		   if pUser:GetNowHP() > 0 then
		      if pNPCUnit:GetRandVal() % 2 == 0 or i == userListSize then
				vPos = pUser:GetPos()
				vPos.y = pNPCUnit:GetPos().y
				pNPCUnit:SetPosition( vPos, pNPCUnit:GetIsRight() )
				return
		      end
		   end
		end
	end

 

