function MovePos( pos, dirvector, dist )
	
	pos.x = pos.x + dist * dirvector.x
	pos.y = pos.y + dist * dirvector.y
	pos.z = pos.z + dist * dirvector.z
	
	return pos
	
end

function MONKEY_KING_STOMP_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )

	if pNPCUnit:AnimEventTimer_LUA( 0.666 ) then
	
		pX2Game:GetX2Camera():GetCamera():UpDownCrashCameraNoReset( 5.0, 1.5 )

	end
	
	if pNPCUnit:AnimEventTimer_LUA( 3.233 ) then
	
		pX2Game:GetX2Camera():GetCamera():UpDownCrashCameraNoReset( 20, 0.4 )

	end



	if pNPCUnit:AnimEventTimer_LUA( 3.1 ) then
	
		vPos			= pNPCUnit:GetPos()
		bIsRight		= pNPCUnit:GetIsRight()
		vDirVector		= pNPCUnit:GetDirVector()
		vLandPos		= pNPCUnit:GetLandPosition_LUA()
		
		
		
		
		if bIsRight == true then 
			vLandPos = MovePos( vPos, vDirVector, 200 )
		else
			vLandPos = MovePos( vPos, vDirVector, -200 )
		end
		
		vLandPos.y = vLandPos.y + 2
	
		pMajorParticle = pX2Game:GetMajorParticle()
		pMajorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "Ground_ShockWave_Monkey_King_Stomp01", vLandPos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
	end
	
	if pNPCUnit:AnimEventTimer_LUA( 3.233 ) then
	
		pMajorParticle = pX2Game:GetMajorParticle()
	
		vPos			= pNPCUnit:GetPos()
		bIsRight		= pNPCUnit:GetIsRight()
		vDirVector		= pNPCUnit:GetDirVector()
		vLandPos		= pNPCUnit:GetLandPosition_LUA()
		
		
		if bIsRight == true then 
			vLandPos = MovePos( vPos, vDirVector, 200 )
		else
			vLandPos = MovePos( vPos, vDirVector, -200 )
		end
	
		pMajorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "Stone_Monkey_Kine_Stomp01", vLandPos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
		pMajorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "Stone_Monkey_Kine_Stomp02", vLandPos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
	end

end


