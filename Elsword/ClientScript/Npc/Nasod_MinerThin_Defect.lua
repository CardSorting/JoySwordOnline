function NASOD_MINER_THIN_DEFECT_RUMBLE_ATTACK_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )
	
	
	if pNPCUnit:AnimEventTimer_LUA( 0.860 ) then
		pNPCUnit:ClearHitUnitList_LUA()
		pNPCUnit:SetDamageData_LUA( "DAMAGE_DATA_LAST" )
		pNPCUnit:SetSpeedX( INIT_PHYSIC["RUN_SPEED"] )
	end

	if pNPCUnit:AnimEventTimer_LUA( 0.91 ) then
		pNPCUnit:ClearHitUnitList_LUA()
		pNPCUnit:SetDamageData_LUA( "DAMAGE_DATA_LAST" )
		pNPCUnit:SetSpeedX( INIT_PHYSIC["RUN_SPEED"] )
	end	
	
	if pNPCUnit:AnimEventTimer_LUA( 0.96 ) then
		pNPCUnit:ClearHitUnitList_LUA()
		pNPCUnit:SetDamageData_LUA( "DAMAGE_DATA_LAST" )
		pNPCUnit:SetSpeedX( INIT_PHYSIC["RUN_SPEED"] )
	end
	
	if pNPCUnit:AnimEventTimer_LUA( 1.01 ) then
		pNPCUnit:ClearHitUnitList_LUA()
		pNPCUnit:SetDamageData_LUA( "DAMAGE_DATA_LAST" )
		pNPCUnit:SetSpeedX( INIT_PHYSIC["RUN_SPEED"] )
	end
	
	
	pMinorParticle = pX2Game:GetMinorParticle()
	
	if pNPCUnit:AnimEventTimer_LUA( 0.01 ) then
	
		vRotateDegree	= pNPCUnit:GetRotateDegree()
		vBonePos		= pNPCUnit:GetBonePos_LUA( "Dummy01" )
	    pNPCUnit:PlaySound_LUA( "Nasod_Engine_Small.ogg" )
	
		pParticle = pMinorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "Smoke_Nasod_MinerThin_Attack", vBonePos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
		if pParticle ~= nil then
			pParticle:SetAddRotate( vRotateDegree )
			pParticle:SetAxisAngle( vRotateDegree )	
			pNPCUnit:SetMinorParticle_LUA( 0, pParticle:GetHandle() ) 
		end
	
	end
	
	
	pParticle = pNPCUnit:GetMinorParticle_LUA( 0 ) 
	if nil ~= pParticle then 
	
		if pMinorParticle:IsLiveInstance( pParticle ) == true then 
			vBonePos		= pNPCUnit:GetBonePos_LUA( "Dummy01" )
			pParticle:SetPosition( vBonePos )		
		else 
			pNPCUnit:ClearMinorParticle_LUA( 0 )
		end
	
	end

end



