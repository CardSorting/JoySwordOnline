function MovePos( pos, dirvector, dist )
	
	pos.x = pos.x + dist * dirvector.x
	pos.y = pos.y + dist * dirvector.y
	pos.z = pos.z + dist * dirvector.z
	
	return pos	
end

function InitSelfBomb_Thin(pNPCUnit)	

    pNPCUnit:SetShow(true)
    
    pNPCUnit:SetFlag_LUA(0, false)  
    pNPCUnit:SetInt_LUA(0, 4)
    
    vec = pNPCUnit:GetPos()
    vec.x = 0.0
    vec.y = 0.0
    vec.z = 0.0
    pNPCUnit:SetVector_LUA(0, vec )
end

function SelfBomb_Ready_Thin(pKTDXApp, pX2Game, pNPCUnit)

    pMajorParticle = pX2Game:GetMajorParticle()
    pParticle = pNPCUnit:GetMajorParticle_LUA( 0 ) 
    
	if nil ~= pParticle and pMajorParticle ~= nil then 
		if pMajorParticle:IsLiveInstance( pParticle ) == true then 
			vPos = pNPCUnit:GetPos()
			vPos.y = vPos.y + 300.0
			pParticle:SetPosition( vPos )		
		else 
			pNPCUnit:ClearMajorParticle_LUA( 0 )
		end
	end
	    
    IsBomb = pNPCUnit:GetFlag_LUA(0) 
    nCount = pNPCUnit:GetInt_LUA(0)
    vec = pNPCUnit:GetVector_LUA(0)
    nPreTime = vec.x
    nCurrTime = pX2Game:GetGameTime()   
    
    if nCount <= 0 then
        -- 자폭상태        
        return true
    end
    
    if pNPCUnit:GetNowHP() <= pNPCUnit:GetMaxHP() * 0.3 then
        -- 자폭진행상태
        
        pNPCUnit:SetSuperArmor(true)
        
        if IsBomb == true then  
            -- 이미 자폭진행상태
            nStep = math.floor(nCurrTime - nPreTime)
            
            pNPCUnit:SetUseWorldColor( false )
	        color = pNPCUnit:GetColor()
	        color.g = color.g - (0.3 * pKTDXApp:GetElapsedTime())
	        color.b = color.g - (0.3 * pKTDXApp:GetElapsedTime())
	        pNPCUnit:SetColor( color )	
            
            if nStep >= 1.0 then
                -- 자폭 카운트 증가
                nNewCount = nCount - nStep
                if nNewCount <= 0 then
                    nNewCount = 0
                end
                
                vec.x = nCurrTime                    
                pNPCUnit:SetInt_LUA(0, nNewCount)
                pNPCUnit:SetVector_LUA(0, vec)
            else
                -- 현재 상태 유지
                return false            
            end                        
        else
            -- 자폭상태 돌입
            vec.x = nCurrTime
            pNPCUnit:SetFlag_LUA(0, true)   
            pNPCUnit:SetInt_LUA(0, 3)
            pNPCUnit:SetVector_LUA(0, vec )
            
            nNewCount = 3
        end 
    else
        return false               
    end

    vPos = pNPCUnit:GetPos()
	vPos.y = vPos.y + 300.0
	       		    
		    		    
    if nNewCount == 3 then    	
		pMajorParticle = pX2Game:GetMajorParticle()	
		-- count 3
    	pNPCUnit:PlaySound_LUA( "MineCountDown.ogg" )
		pParticle = pMajorParticle:GameUnitCreateSequence_LUA( pNPCUnit, "Mine_Countdown", vPos, D3DXVECTOR2(-1,-1), D3DXVECTOR2(-1,-1) )
		pNPCUnit:SetMajorParticle_LUA( 0, pParticle:GetHandle() ) 
		
    elseif nNewCount == 2 then
        pParticle = pNPCUnit:GetMajorParticle_LUA( 0 ) 
     	pNPCUnit:PlaySound_LUA( "MineCountDown.ogg" )
       if nil ~= pParticle then 
			if pMajorParticle:IsLiveInstance( pParticle ) == true then 
                pParticle:ChangeTexForce_LUA( "PC_2.dds" )
            end
        end                
        
    elseif nNewCount == 1 then
        pParticle = pNPCUnit:GetMajorParticle_LUA( 0 ) 
     	pNPCUnit:PlaySound_LUA( "MineCountDown.ogg" )
       if nil ~= pParticle then 
			if pMajorParticle:IsLiveInstance( pParticle ) == true then 
                pParticle:ChangeTexForce_LUA( "PC_1.dds" )
            end
        end     
        
    elseif nNewCount == 0 then
        -- 자폭상태
        pMajorParticle = pX2Game:GetMajorParticle()
	    pParticle = pNPCUnit:GetMajorParticle_LUA( 0 )
	    if pParticle ~= nil then
		    pMajorParticle:DestroyInstance( pParticle )
		    pNPCUnit:ClearMajorParticle_LUA( 0 )
	    end
        return true
    end
    
    return false
end

function SelfBomb_Thin(pX2Game, pNPCUnit)

    pNPCUnit:SetShow(true)
            
    if pNPCUnit:GetInt_LUA(0) == 5 then
        return
    end
            
    pNPCUnit:SetNowHP_LUA(0)
    
    pMajorParticle = pX2Game:GetMajorParticle()
	pParticle = pNPCUnit:GetMajorParticle_LUA( 0 )
	if pParticle ~= nil then
		pMajorParticle:DestroyInstance( pParticle )
		pNPCUnit:ClearMajorParticle_LUA( 0 )
	end
    
    pDamageEffect = pX2Game:GetDamageEffect()
    landPos = pNPCUnit:GetLandPosition_LUA()
    vPos = pNPCUnit:GetPos()    
    
    if pDamageEffect ~= nil then
	    pDamageEffect:CreateInstance_LUA( pNPCUnit, "NASOD_MONSTER_EXPLOSION", vPos, landPos.y)
	    pNPCUnit:SetShow(false)
	    pNPCUnit:SetInt_LUA(0, 5)
    end			
end
