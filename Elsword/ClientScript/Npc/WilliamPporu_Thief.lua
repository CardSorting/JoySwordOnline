function WILLIAM_PPORU_THIEF_ATTACK_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )



	-- attack time 1.5 ~ 2.5

	for i=1, 5 do
	
		fTime = 1.5 + i * 0.16
		
		if pNPCUnit:AnimEventTimer_LUA( fTime ) then
			pNPCUnit:ClearHitUnitList_LUA()
	
			if i == 5 then
				pNPCUnit:SetDamageData_LUA( "DAMAGE_DATA_LAST" )
			end

		end
		
	end

end


function WILLIAM_PPORU_THIEF_COMBO_ATTACK_A_FRAME_MOVE( pKTDXApp, pX2Game, pNPCUnit )
			
	if pNPCUnit:AnimEventTimer_LUA( 0.01 ) then
		pNPCUnit:SetSpeedX( INIT_PHYSIC["WALK_SPEED"] )
	end
	
	if pNPCUnit:AnimEventTimer_LUA( 1.2 ) then
		pNPCUnit:ClearHitUnitList_LUA()
	end
	
	if pNPCUnit:AnimEventTimer_LUA( 1.2 ) then
		pNPCUnit:SetSpeedX( INIT_PHYSIC["WALK_SPEED"] )
	end
	
	if pNPCUnit:AnimEventTimer_LUA( 2.2 ) then
		pNPCUnit:ClearHitUnitList_LUA()
		pNPCUnit:SetDamageData_LUA( "DAMAGE_DATA_LAST" )
	end
		
	if pNPCUnit:AnimEventTimer_LUA( 2.2) then
		pNPCUnit:SetSpeedX( INIT_PHYSIC["WALK_SPEED"] )
	end
		
end



