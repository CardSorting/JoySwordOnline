    SET @NO_REMAIN_CASH = @NO_BEFOR_CASH - @NO_PRICE;
    SET @NO_REMAIN_BONUS = @NO_BEFOR_BONUS;

    UPDATE dbo.EB_Cash
    SET NO_REMAIN_CASH = @NO_REMAIN_CASH
    WHERE CD_USERUID = @CD_USERUID;

    RETURN 0;
END;
GO

